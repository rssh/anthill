fn render_as_math_constant(fn_qn: &str) -> Option<String> {
    let table: &[(&str, &str)] = &[
        ("Float.pi",  "3.141592653589793"),
        ("Float.e",   "2.718281828459045"),
        ("Float.tau", "6.283185307179586"),
    ];
    lookup_prelude_qn(fn_qn, table).map(str::to_string)
}

/// Lower the bodyless host-supplied Float constants (WI-532): IEEE special
/// values that have no C++ literal spelling. Emitting `std::numeric_limits`
/// pulls in `<limits>` via the `"std::numeric_limits"` `IncludeMapping` fact
/// when the surrounding block is scanned. Kept separate from
/// `render_as_math_constant` (whose payloads are bare literals, no header).
fn render_as_float_special(fn_qn: &str) -> Option<String> {
    let table: &[(&str, &str)] = &[
        ("Float.infinity",         "std::numeric_limits<double>::infinity()"),
        ("Float.negativeInfinity", "-std::numeric_limits<double>::infinity()"),
        ("Float.nan",              "std::numeric_limits<double>::quiet_NaN()"),
    ];
    lookup_prelude_qn(fn_qn, table).map(str::to_string)
}

/// Match a fully-qualified or short prelude name against a (suffix,
/// payload) table. `suffix` is the qn after `anthill.prelude.`.
/// Accepts both `anthill.prelude.<suffix>` (qualified) and `<suffix>`
/// (short — produced when imports leave a name partly-qualified).
fn lookup_prelude_qn<'a, T: Copy>(fn_qn: &str, table: &'a [(&'a str, T)]) -> Option<T> {
    let stripped = fn_qn.strip_prefix("anthill.prelude.").unwrap_or(fn_qn);
    table.iter().find(|(suffix, _)| *suffix == stripped).map(|(_, v)| *v)
}

/// Lower `IndexedSeq.nth(xs, i)` and `IndexedSeq.length(xs)` to
/// container-agnostic C++ — works for any host type with `size()`
/// and `operator[]` (`std::vector`, `std::array`, hand-rolled, …).
/// `nth` checks bounds and wraps in `std::optional`.
fn render_as_indexed_seq(fn_qn: &str, args: &[String]) -> Option<String> {
    let stripped = fn_qn.strip_prefix("anthill.prelude.").unwrap_or(fn_qn);
    match stripped {
        "IndexedSeq.length" if args.len() == 1 => {
            Some(format!("static_cast<int64_t>({}.size())", args[0]))
        }
        "IndexedSeq.nth" if args.len() == 2 => {
            let xs = &args[0];
            let i = &args[1];
            Some(format!(
                "(({i} >= 0 && static_cast<size_t>({i}) < {xs}.size()) \
                 ? std::make_optional({xs}[{i}]) : std::nullopt)"
            ))
        }
        _ => None,
    }
}

/// Render a known prelude typeclass call as a C++ operator expression.
/// Returns `None` for non-typeclass functions, which the caller then
/// emits as a plain function call.
///
/// Why string-keyed: typeclass facts (`fact Numeric{T = Int64}`) make the
/// dispatch decision a typeclass-membership check, but for primitives
/// the answer is always "yes" — and our scrutinees today are almost
/// always primitives. The user's own sorts can overload the C++
/// operator if they want to participate, or they keep using the named
/// function form (which would round-trip through this table since the
/// QN wouldn't match — but the QN of a user's own `add` differs).
fn render_as_operator(fn_qn: &str, args: &[String]) -> Option<String> {
    // (qualified_name, arity, infix_or_prefix_op)
    let infix: &[(&str, &str)] = &[
        ("anthill.prelude.Numeric.add", "+"),
        ("anthill.prelude.Numeric.sub", "-"),
        ("anthill.prelude.Numeric.mul", "*"),
        ("anthill.prelude.Int64.div",     "/"),
        ("anthill.prelude.Float.div",   "/"),
        ("anthill.prelude.Int64.mod",     "%"),
        ("anthill.prelude.Ordered.gt",  ">"),
        ("anthill.prelude.Ordered.lt",  "<"),
        ("anthill.prelude.Ordered.gte", ">="),
        ("anthill.prelude.Ordered.lte", "<="),
        ("anthill.prelude.Eq.eq",       "=="),
        ("anthill.prelude.Eq.neq",      "!="),
        ("anthill.prelude.Bool.and",    "&&"),
        ("anthill.prelude.Bool.or",     "||"),
    ];
    for (qn, op) in infix {
        if fn_qn == *qn && args.len() == 2 {
            return Some(format!("({} {} {})", args[0], op, args[1]));
        }
    }
    let prefix: &[(&str, &str)] = &[
        ("anthill.prelude.Bool.not",    "!"),
        ("anthill.prelude.Int64.neg",     "-"),
        ("anthill.prelude.Float.neg",   "-"),
    ];
    for (qn, op) in prefix {
        if fn_qn == *qn && args.len() == 1 {
            return Some(format!("({}{})", op, args[0]));
        }
    }
    None
}

/// Lower stdlib wrapper-constants used as bare values (e.g. `none`
/// on its own, without parens). Returns `Some(s)` when matched.
fn lower_stdlib_constant(fn_qn: &str) -> Option<String> {
    let table: &[(&str, &str)] = &[
        ("Option.none", "std::nullopt"),
    ];
    // Also handle the bare-import case (`none`) which strip_prefix can't reach.
    if fn_qn == "none" { return Some("std::nullopt".to_string()); }
    lookup_prelude_qn(fn_qn, table).map(str::to_string)
}

/// Lower stdlib wrapper-constructor calls (Option, Result, …) from
/// NodeOccurrence arg slices. Returns `Ok(Some(s))` when matched,
/// `Ok(None)` when the functor is not one we recognise. Today:
/// `Option.{some, none}`.
fn lower_stdlib_wrapper_node(
    ctx: &CodegenContext,
    fn_qn: &str,
    pos_args: &[std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>],
    named_args: &[(Symbol, std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>)],
) -> Result<Option<String>, CppCodegenError> {
    if let Some(c) = lower_stdlib_constant(fn_qn) {
        return Ok(Some(c));
    }
    let stripped = fn_qn.strip_prefix("anthill.prelude.").unwrap_or(fn_qn);
    if stripped == "Option.some" || fn_qn == "some" {
        let mut parts = Vec::new();
        for a in combined_args(pos_args, named_args) {
            parts.push(lower_node(ctx, a)?);
        }
        if parts.len() != 1 {
            return Err(CppCodegenError {
                message: format!("Option.some expects 1 arg, got {}", parts.len()),
            });
        }
        return Ok(Some(format!("std::make_optional({})", parts[0])));
    }
    Ok(None)
}

/// Lower an entity-constructor application from NodeOccurrence args
/// to brace-initialiser syntax: `EntityName{val0, val1, ...}`. Args
/// may be positional or named; either way values are emitted in the
/// entity's field declaration order so the resulting C++ matches the
/// struct layout emitted by `emit_entity_struct_by_symbol`.
fn lower_constructor_literal_node(
    ctx: &CodegenContext,
    entity_sym: Symbol,
    pos_args: &[std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>],
    named_args: &[(Symbol, std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>)],
) -> Result<String, CppCodegenError> {
    use std::collections::HashMap;
    let kb = ctx.kb;
    let qn = kb.qualified_name_of(entity_sym);
    let short = short_name_of(qn);
    let short_name = qualify_cross_namespace(ctx, qn, short);
    let fields = kb.entity_field_types(entity_sym).ok_or_else(|| {
        CppCodegenError { message: format!("'{qn}' has no registered fields") }
    })?;

    let mut named_values: HashMap<String, String> = HashMap::new();
    let mut positional: Vec<String> = Vec::new();
    for arg in pos_args.iter() {
        positional.push(lower_node(ctx, arg)?);
    }
    for (name_sym, arg) in named_args.iter() {
        let n = kb.resolve_sym(*name_sym).to_string();
        named_values.insert(n, lower_node(ctx, arg)?);
    }

    let mut vals = Vec::with_capacity(fields.len());
    for (i, (field_sym, _)) in fields.iter().enumerate() {
        let field_name = kb.resolve_sym(*field_sym).to_string();
        if let Some(v) = named_values.remove(&field_name) {
            vals.push(v);
        } else if let Some(v) = positional.get(i) {
            vals.push(v.clone());
        } else {
            return Err(CppCodegenError {
                message: format!(
                    "constructor literal '{short_name}': no value for field '{field_name}'"
                ),
            });
        }
    }
    Ok(format!("{short_name}{{{}}}", vals.join(", ")))
}

/// Lower a sequence of `MatchBranch` terms over a scrutinee expression
/// to a chain of `<tag-check> ? <body> : <next>` ternaries. The
/// final branch becomes the catch-all — exhaustiveness is the
/// source's responsibility (the type checker rejects non-exhaustive
/// matches before codegen).
///
/// Branches with binding patterns (e.g. `case some(?w) -> body`)
/// emit the body inside an IIFE that introduces the bound names.
/// `Option.some` / `Option.none` are special-cased to use
/// `o.has_value()` / `o.value()` instead of the variant primitives,
/// since Option lowers to `std::optional<T>` rather than a variant.
fn lower_match_branches_node(
    ctx: &CodegenContext,
    scrutinee: &str,
    branches: &[anthill_core::kb::node_occurrence::MatchBranch],
) -> Result<String, CppCodegenError> {
    let kb = ctx.kb;
    struct Compiled {
        tag_check: Option<String>, // None = catch-all (wildcard)
        body: String,
    }
    let mut compiled: Vec<Compiled> = Vec::with_capacity(branches.len());
    for branch in branches {
        // WI-318: branch.pattern is a Pattern-kind occurrence.
        let info = analyse_pattern_occ(kb, &branch.pattern, scrutinee)?;
        // Bindings frame: `?w` in the body refers to the local
        // `auto w` introduced by the IIFE, not to the access
        // expression — the latter could re-trigger side effects.
        let mut frame: std::collections::HashMap<String, String> =
            std::collections::HashMap::new();
        for (name, _access) in &info.decls {
            frame.insert(name.clone(), name.clone());
        }
        let _guard = ctx.push_value_bindings(frame);
        let body_s = lower_node(ctx, &branch.body)?;
        let body_with_bindings = if info.decls.is_empty() {
            body_s
        } else {
            let mut decls = String::new();
            for (name, access) in &info.decls {
                decls.push_str(&format!("auto {name} = {access}; "));
            }
            format!("[&]() {{ {decls}return {body_s}; }}()")
        };
        compiled.push(Compiled { tag_check: info.tag_check, body: body_with_bindings });
    }
    // Right-to-left fold so the first branch appears outermost.
    let last = compiled.pop().expect("caller ensured at least one branch");
    let mut out = last.body;
    while let Some(c) = compiled.pop() {
        let tag = c.tag_check.ok_or_else(|| CppCodegenError {
            message: "match: a non-final branch had a wildcard pattern \
                      (catch-all only allowed last)".into(),
        })?;
        out = format!("({tag} ? {body} : {out})", body = c.body);
    }
    Ok(out)
}

/// Result of analysing one `MatchBranch`'s pattern: the tag-check
/// expression (None for wildcard catch-alls), and the value bindings
/// the pattern introduces. `decls` is what the IIFE should emit as
/// local declarations (e.g. `("w", "o.value()")` becomes
/// `auto w = o.value();`); the body's binding map then resolves
/// references to the *local* names, not the access expressions, so
/// the local is used instead of re-evaluating the access.
struct PatternInfo {
    tag_check: Option<String>,
    decls: Vec<(String, String)>,
}

/// Compile a pattern against `scrutinee` into a tag check + binding map.
/// Recognised forms:
/// - `Pattern.var_pattern{name: <Entity>}` — nullary constructor pattern.
///   For Option's `none` (qn `anthill.prelude.Option.none`), uses
///   `!s.has_value()`. Otherwise `std::holds_alternative<X>(s)`.
/// - `Pattern.constructor_pattern{name, args}` — N-ary constructor with
///   sub-patterns. For `Option.some(?w)` uses `s.has_value()` and binds
///   `w → s.value()`. For other entities, binds each sub-`var_pattern`
///   to `std::get<Ctor>(s).<field_name>` in declaration order.
/// - `Pattern.wildcard` — no tag check (None), no bindings.
/// WI-318: Pattern-occurrence variant of `analyse_pattern`.
/// Dispatches on the structural `Pattern` enum instead of round-
/// tripping through a TermId.
fn analyse_pattern_occ(
    kb: &KnowledgeBase,
    occ: &std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>,
    scrutinee: &str,
) -> Result<PatternInfo, CppCodegenError> {
    use anthill_core::kb::node_occurrence::Pattern;
    let Some(pat) = occ.as_pattern() else {
        return Err(CppCodegenError {
            message: "expected Pattern-kind occurrence in match branch (WI-318)".into(),
        });
    };
    match pat {
        Pattern::Wildcard => Ok(PatternInfo {
            tag_check: None,
            decls: Vec::new(),
        }),
        Pattern::Var { name, .. } => {
            let ctor_qn = kb.qualified_name_of(*name).to_string();
            let tag = nullary_tag_check(&ctor_qn, scrutinee);
            Ok(PatternInfo { tag_check: Some(tag), decls: Vec::new() })
        }
        Pattern::Constructor { name, pos_args, .. } => {
            let ctor_sym = *name;
            let ctor_qn = kb.qualified_name_of(ctor_sym).to_string();
            let short = short_name_of(&ctor_qn);
            // Option.some(?w) special case mirrors the term-based path.
            if ctor_qn == "anthill.prelude.Option.some" || ctor_qn == "Option.some" {
                if pos_args.len() != 1 {
                    return Err(CppCodegenError {
                        message: format!("Option.some pattern expects 1 sub-pattern, got {}", pos_args.len()),
                    });
                }
                let bind_name = pattern_var_name_occ(kb, &pos_args[0])?;
                return Ok(PatternInfo {
                    tag_check: Some(format!("{scrutinee}.has_value()")),
                    decls: vec![(bind_name, format!("{scrutinee}.value()"))],
                });
            }
            if ctor_qn == "anthill.prelude.Option.none" || ctor_qn == "Option.none" {
                return Ok(PatternInfo {
                    tag_check: Some(format!("!{scrutinee}.has_value()")),
                    decls: Vec::new(),
                });
            }
            // Generic entity constructor: variant tag + per-sub-pattern
            // bind into `std::get<Ctor>(s).<field>`. Mirrors the pre-WI-318
            // term-based path's error reporting — fail loudly on unknown
            // entity and arity mismatch rather than emit invalid C++.
            let tag = format!("std::holds_alternative<{short}>({scrutinee})");
            let fields = kb.entity_field_types(ctor_sym).ok_or_else(|| CppCodegenError {
                message: format!(
                    "constructor pattern '{ctor_qn}' is not a known entity"
                ),
            })?;
            if pos_args.len() != fields.len() {
                return Err(CppCodegenError {
                    message: format!(
                        "constructor pattern '{ctor_qn}' expects {} sub-patterns, got {}",
                        fields.len(),
                        pos_args.len()
                    ),
                });
            }
            let mut decls: Vec<(String, String)> = Vec::new();
            for (sub_pat, (field_sym, _)) in pos_args.iter().zip(fields.iter()) {
                if matches!(sub_pat.as_pattern(), Some(Pattern::Wildcard)) {
                    continue;
                }
                let bind_name = pattern_var_name_occ(kb, sub_pat)?;
                let field_name = kb.resolve_sym(*field_sym);
                decls.push((
                    bind_name,
                    format!("std::get<{short}>({scrutinee}).{field_name}"),
                ));
            }
            Ok(PatternInfo { tag_check: Some(tag), decls })
        }
        Pattern::Tuple { .. } | Pattern::Literal { .. } => Err(CppCodegenError {
            message: format!("match pattern not yet supported in cpp-gen: {pat:?}"),
        }),
    }
}

/// Tag check for a nullary-constructor pattern. Special-cases Option.none.
fn nullary_tag_check(ctor_qn: &str, scrutinee: &str) -> String {
    if ctor_qn == "anthill.prelude.Option.none" || ctor_qn == "Option.none" {
        return format!("!{scrutinee}.has_value()");
    }
    let short = short_name_of(ctor_qn);
    format!("std::holds_alternative<{short}>({scrutinee})")
}

/// Extract the bound name from a `Pattern.var_pattern(name: Ref(<n>), ...)`.
/// Used by let / lambda lowering. Constructor / tuple / wildcard /
/// literal pattern shapes are handled by `pattern_constructor_name`
/// in the match path instead. WI-318: pre-lift sibling of
/// `pattern_var_name_occ`, kept for the term-form code paths still in
/// use (constructor analysis fallbacks); the let/lambda lowering uses
/// the `_occ` variant.
#[allow(dead_code)] // WI-318: pre-lift sibling, kept for future fallback paths.
fn pattern_var_name(kb: &KnowledgeBase, pat: TermId) -> Result<String, CppCodegenError> {
    if let Term::Fn { functor, named_args, .. } = kb.get_term(pat) {
        let qn = kb.qualified_name_of(*functor);
        if qn == "anthill.reflect.Pattern.var_pattern" {
            if let Some(name_term) = find_named(kb, named_args, "name") {
                if let Term::Ref(s) | Term::Ident(s) = kb.get_term(name_term) {
                    let qn = kb.qualified_name_of(*s);
                    return Ok(short_name_of(qn).to_string());
                }
            }
        }
        return Err(CppCodegenError {
            message: format!("let/lambda binder: only var_pattern supported, got {qn}"),
        });
    }
    Err(CppCodegenError {
        message: format!("expected var_pattern, got {:?}", kb.get_term(pat)),
    })
}

/// WI-318: Pattern-occurrence variant of `pattern_var_name`. Reads the
/// bound name directly from `Pattern::Var`; other Pattern variants are
/// not yet supported in let/lambda binder position by cpp-gen.
fn pattern_var_name_occ(
    kb: &KnowledgeBase,
    occ: &std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>,
) -> Result<String, CppCodegenError> {
    use anthill_core::kb::node_occurrence::Pattern;
    match occ.as_pattern() {
        Some(Pattern::Var { name, .. }) => {
            let qn = kb.qualified_name_of(*name);
            Ok(short_name_of(qn).to_string())
        }
        Some(other) => Err(CppCodegenError {
            message: format!("let/lambda binder: only Var pattern supported, got {other:?}"),
        }),
        None => Err(CppCodegenError {
            message: "let/lambda binder: expected Pattern-kind occurrence".into(),
        }),
    }
}

/// Lower a `Literal` to its C++ source spelling. String literals get
/// the same `\"` / `\\` / `\n` / `\r` / `\t` escaping the printer uses.
fn lower_literal(lit: &Literal) -> String {
    match lit {
        Literal::Int(n) => n.to_string(),
        Literal::Float(f) => {
            let s = f.to_string();
            if s.contains('.') { s } else { format!("{s}.0") }
        }
        Literal::Bool(b) => if *b { "true" } else { "false" }.to_string(),
        Literal::String(s) => {
            let mut out = String::with_capacity(s.len() + 2);
            out.push('"');
            for c in s.chars() {
                match c {
                    '"'  => out.push_str("\\\""),
                    '\\' => out.push_str("\\\\"),
                    '\n' => out.push_str("\\n"),
                    '\r' => out.push_str("\\r"),
                    '\t' => out.push_str("\\t"),
                    _ => out.push(c),
                }
            }
            out.push('"');
            out
        }
        Literal::Handle(kind, id) => format!("/* handle {kind:?}:{id} */"),
        // BigInt has no native C++17 stdlib mapping — caller would
        // need a custom carrier. Emitted as a comment so the failure
        // is loud.
        other => format!("/* unsupported literal: {other:?} */"),
    }
}

fn lower_type(ctx: &CodegenContext, type_term: TermId) -> Result<String, CppCodegenError> {
    let kb = ctx.kb;
    if let Some(sym) = extract_sort_ref_sym(kb, &TermIdView(type_term)) {
        return sort_to_cpp(ctx, sym);
    }

    let term = kb.get_term(type_term);
    match term {
        Term::Ref(sym) | Term::Ident(sym) => sort_to_cpp(ctx, *sym),
        // Logic variable in a type position — references to a sort's
        // declared type parameter (`?T`) lower to the C++ template
        // parameter name. The Var's `name` symbol matches the
        // `add_type_param` entry in the sort body's scope.
        Term::Var(v) => {
            let name_sym = match v {
                anthill_core::kb::term::Var::Global(vid) => vid.name(),
                anthill_core::kb::term::Var::DeBruijn(_) => return Err(CppCodegenError {
                    message: "DeBruijn var in type position — should have been opened".into(),
                }),
                anthill_core::kb::term::Var::Rigid(_) => return Err(CppCodegenError {
                    message: "Rigid var in type position — only valid during proof".into(),
                }),
            };
            let name = kb.resolve_sym(name_sym).to_string();
            if let Some(cpp) = ctx.lookup_type_param(&name) {
                return Ok(cpp);
            }
            Err(CppCodegenError {
                message: format!(
                    "type variable '?{name}' is not bound by any enclosing \
                     sort's type parameters — declare `sort {name} = ?` in \
                     the sort body, or pass an explicit binding"
                ),
            })
        }
        Term::Fn { functor, .. } => {
            // Parameterized type — the deep `parameterized(base: sort_ref(S),
            // bindings)` OR the term-backed `Fn{S, named}` form (WI-361).
            // `unpack_parameterized` reads both; a non-parameterized Fn (e.g.
            // an `arrow`/`named_tuple` structural type) falls through to error.
            if unpack_parameterized(kb, type_term).is_some() {
                return lower_parameterized(ctx, type_term);
            }
            let qualified = kb.qualified_name_of(*functor);
            Err(CppCodegenError {
                message: format!(
                    "unexpected non-parameterized Fn term as type: '{qualified}' \
                     (expected a sort ref, a parameterized type, or a term-backed Fn{{S, named}})"
                ),
            })
        }
        other => Err(CppCodegenError {
            message: format!("cannot lower term to C++ type: {other:?}"),
        }),
    }
}

/// Lower a `parameterized(base: sort_ref(...), bindings: [...])` term
/// to its C++ template form. Base resolves via carrier or the keyed
/// `TypeMapping` query (`cpp_base_host_type`);
/// each binding's `value` is recursively lowered into the angle-bracket
/// arg list, in TypeBinding declaration order.
fn lower_parameterized(
    ctx: &CodegenContext,
    type_term: TermId,
) -> Result<String, CppCodegenError> {
    let kb = ctx.kb;
    let (base_sym, binding_pairs) = unpack_parameterized(kb, type_term).ok_or_else(|| {
        CppCodegenError {
            message: "expected a parameterized(base: ..., bindings: [...]) term".into(),
        }
    })?;

    let qualified = kb.qualified_name_of(base_sym);
    let short = short_name_of(qualified);
    let template_name = ctx.carriers.lookup(qualified)
        .map(str::to_string)
        .or_else(|| resolve_type_mapping(ctx, &[short], None).map(|h| h.host_type))
        .ok_or_else(|| CppCodegenError {
            message: format!(
                "no C++ mapping for parameterized sort '{qualified}' — add a \
                 CarrierBinding, or a `fact TypeMapping(lang: some(\"cpp\"), \
                 anthill_type: \"{short}\", host_type: ...)`"
            ),
        })?;

    // C++ template args follow the sort's DECLARATION order (`template<K, V>`),
    // but `binding_pairs` are stored in canonical symbol-interning order (WI-361
    // flip). Reorder by `type_params_of_sort` (source/declaration order); fall back
    // to stored order if the sort exposes no declared params (defensive).
    let decl_order = kb.type_params_of_sort(base_sym);
    let ordered: Vec<TermId> = if decl_order.is_empty() {
        binding_pairs.iter().map(|(_, v)| *v).collect()
    } else {
        let mut out: Vec<TermId> = decl_order
            .iter()
            .filter_map(|pname| {
                binding_pairs
                    .iter()
                    .find(|(p, _)| kb.resolve_sym(*p) == pname.as_str())
                    .map(|(_, v)| *v)
            })
            .collect();
        // Defensive: append any binding whose param wasn't in the declared set
        // (over-application — not produced by well-formed types) so nothing is lost.
        for (p, v) in &binding_pairs {
            if !decl_order.iter().any(|n| kb.resolve_sym(*p) == n.as_str()) {
                out.push(*v);
            }
        }
        out
    };

    let mut args = Vec::with_capacity(ordered.len());
    for value in ordered {
        args.push(lower_type(ctx, value)?);
    }

    if args.is_empty() {
        // Parameterized form with no bindings — degenerate, but emit
        // the bare template name so we don't error.
        Ok(template_name)
    } else {
        Ok(format!("{template_name}<{}>", args.join(", ")))
    }
}

/// Map a sort symbol to its C++ type spelling. Lookup order:
///   1. `CarrierTable` (from `Implementation` / `CarrierBinding` facts)
///   2. Keyed `TypeMapping` query (`cpp_base_host_type`, WI-089) — the
///      fact-driven base renames in `anthill.realization.cpp_std`
///   3. Project-local entity → its short name (the struct we emit
///      ourselves lives in the same C++ namespace)
///   4. Error
///
/// Carrier bindings take precedence — a user-supplied
/// `CarrierBinding(sort_name: "Int", host_type: "int32_t")` overrides
/// the default `Int → int64_t` mapping. The keyed `TypeMapping` base is
/// the fallback, not a constraint, and is itself project-extensible
/// (assert another `fact TypeMapping(lang: some("cpp"), ...)`).
fn sort_to_cpp(ctx: &CodegenContext, sym: Symbol) -> Result<String, CppCodegenError> {
    let qualified = ctx.kb.qualified_name_of(sym);
    let short = short_name_of(qualified);
    // Type parameter of any enclosing sort (set via push_type_params
    // by emit_traits / emit_entity). Checked before carriers and
    // primitives so a `?T` reference inside `Box[T = ?]` lowers to
    // the template name `T` even if a stray carrier or primitive
    // happens to share the short name.
    if let Some(cpp) = ctx.lookup_type_param(short) {
        return Ok(cpp);
    }
    if let Some(host) = ctx.carriers.lookup(qualified) {
        // Register the header that declares this carrier's host type
        // so the surrounding namespace header gains a matching
        // `#include` (e.g. `webots::GPS *` → `#include <webots/GPS.hpp>`).
        if let Some(artifact) = ctx.carriers.artifact(qualified) {
            ctx.requested_includes.borrow_mut()
                .insert(format!("#include <{artifact}>"));
        }
        return Ok(host.to_string());
    }
    // WI-089(a): declared-signature position — no binding key is active, so
    // `[profile?, none]` selects a profile overlay or the language base (never
    // a binding's marshalling overlay). `cpp_base_host_type` (base-only) is the
    // profile-agnostic special case of this.
    if let Some(hit) = resolve_type_mapping(ctx, &[short], None) {
        return Ok(hit.host_type);
    }
    // Runtime use of `anthill.reflect.*` (TermRepr, SortInfo, KB, …)
    // and `anthill.persistence.*` (Store, FileStore, SqlStore, …) is
    // refused: cpp17-stl has no term-store / hash-cons infrastructure
    // on the host side, so these can't be lowered to value-typed C++.
    // A future `cpp-meta` profile (or an explicit CarrierBinding) can
    // opt in by mapping the sort to a host type.
    if let Some(profile) = unsupported_runtime_profile(qualified) {
        return Err(CppCodegenError {
            message: format!(
                "profile cpp17-stl does not support runtime {profile} (sort '{qualified}') — \
                 these require term-store infrastructure not provided in C++; \
                 either add an explicit CarrierBinding mapping the sort to a host type, \
                 or omit the operation from the C++ surface"
            ),
        });
    }
    // Project-local entity. Refer by short name when the entity
    // lives in the same C++ namespace block we're currently
    // emitting; otherwise use the fully-qualified path so the
    // generated header compiles regardless of where the consumer
    // sits (e.g. `anthill.geometry.Vec3` referenced from inside an
    // `anthill.examples.lf1` entity must render as
    // `::anthill::geometry::Vec3`).
    if ctx.kb.entity_field_types(sym).is_some() {
        register_cross_namespace_include(ctx, qualified);
        return Ok(qualify_cross_namespace(ctx, qualified, short));
    }
    if !constructors_of(ctx, sym).is_empty() {
        register_cross_namespace_include(ctx, qualified);
        return Ok(qualify_cross_namespace(ctx, qualified, short));
    }
    Err(CppCodegenError {
        message: format!(
            "no C++ mapping for sort '{qualified}' — add a CarrierBinding \
             via an Implementation fact, or model it as an entity with fields"
        ),
    })
}

/// Classify a fully-qualified sort name as belonging to a stdlib
/// subsystem the cpp17-stl profile cannot lower at runtime. Returns
/// the human-readable subsystem label, or `None` if the sort is fine.
fn unsupported_runtime_profile(qualified: &str) -> Option<&'static str> {
    if qualified.starts_with("anthill.reflect.") {
        Some("reflection")
    } else if qualified.starts_with("anthill.persistence.") {
        Some("persistence")
    } else {
        None
    }
}

/// A keyed type-mapping entry resolved from the KB (WI-089). `key = none`
/// is the language base (the default rename); `some(k)` is a profile or
/// foreign-binding overlay queried ahead of the base.
struct TypeMappingHit {
    host_type: String,
    /// foreign->anthill adapter (marshalled overlay only; `None` on a plain
    /// rename). Read back so marshalling rides the SAME query as the renames.
    lift: Option<String>,
    /// anthill->foreign adapter (marshalled overlay only; `None` on a plain
    /// rename).
    lower: Option<String>,
    key: Option<String>,
}

/// WI-089(a): first-match-wins selection over an ordered active-key list. For
/// each key in `active_keys` (most-specific-first), return the first hit whose
/// key equals it — so a binding/profile overlay shadows the base. Generic over
/// the hit type via a key projection, shared by the type- and effect-mapping
/// resolvers. Consumes `hits` (the chosen one is moved out).
fn select_keyed<T>(
    mut hits: Vec<T>,
    key_of: impl Fn(&T) -> &Option<String>,
    active_keys: &[Option<String>],
) -> Option<T> {
    for ak in active_keys {
        if let Some(pos) = hits.iter().position(|h| key_of(h) == ak) {
            return Some(hits.swap_remove(pos));
        }
    }
    None
}

/// Shared machinery for the WI-089 keyed realization-fact queries (TypeMapping,
/// EffectMapping, NamingConvention). Builds an allocation-free `Value::Entity`
/// pattern that grounds `ground_field` to `ground_value` and leaves every other
/// field a fresh placeholder var, then returns the head `TermId` of each
/// matching top-level fact. cpp-gen holds an immutable `&KnowledgeBase`, so it
/// can neither `kb.resolve` (needs `&mut`) nor hash-cons a pattern term; it
/// matches structurally via `kb.query_view`, which keys named args by Symbol —
/// hence the pattern carries the facts' exact field symbols, and the whole
/// query bails (empty) if the functor or any field name isn't interned yet.
/// Callers read the non-ground fields back from each head and apply their own
/// post-filters (e.g. an `Option` `lang`). `functor_names` is tried in order
/// (qualified first, then bare short name).
fn query_realization_facts(
    kb: &KnowledgeBase,
    functor_names: &[&str],
    fields: &[&str],
    ground_field: &str,
    ground_value: &str,
) -> Vec<TermId> {
    let Some(functor) = functor_names.iter().find_map(|n| kb.try_resolve_symbol(n)) else {
        return Vec::new();
    };
    let mut named: Vec<(Symbol, Value)> = Vec::with_capacity(fields.len());
    for (i, fname) in fields.iter().enumerate() {
        let Some(fsym) = kb.lookup_symbol(fname) else {
            return Vec::new();
        };
        let val = if *fname == ground_field {
            Value::Str(ground_value.to_string())
        } else {
            // A distinct placeholder var per non-ground slot. Facts are ground,
            // so a synthetic high VarId cannot collide; results are read from the
            // matched fact head, not from these bindings.
            Value::Var(Var::Global(VarId::new(u32::MAX - i as u32, fsym)))
        };
        named.push((fsym, val));
    }
    let pattern = Value::Entity {
        functor,
        pos: std::rc::Rc::from(Vec::<Value>::new()),
        named: std::rc::Rc::from(named),
    };
    kb.query_view(&pattern)
        .into_iter()
        .map(|(rid, _)| kb.rule_head(rid))
        .collect()
}

/// WI-089: resolve host-type mappings for `anthill_type` under `lang` over the
/// keyed `TypeMapping` facts (the discrim tree IS the table). `lang`/`key` are
/// `Option` fields read back from each matched head — grounding `some("cpp")`
/// structurally would be awkward — so only `anthill_type` is grounded and
/// `lang` is post-filtered. Returns every matching entry across keys; callers
/// pick by key priority.
///
/// WI-089(a): both plain renames and adapter-bearing marshalling overlays flow
/// through THIS one query (the retired `MarshalTable` was a second reader).
/// Only facts with `lang: some("cpp")` are selected; the active-key priority
/// then picks among the matches by `key`. A `TypeMapping` for another language,
/// or one with no `lang`, is simply not a cpp mapping and is not selected.
fn query_type_mappings(kb: &KnowledgeBase, lang: &str, anthill_type: &str) -> Vec<TypeMappingHit> {
    const FIELDS: &[&str] = &["anthill_type", "host_type", "lift", "lower", "lang", "key"];
    let mut hits = Vec::new();
    for head in query_realization_facts(
        kb,
        &["anthill.realization.TypeMapping", "TypeMapping"],
        FIELDS,
        "anthill_type",
        anthill_type,
    ) {
        let fact_lang = named_optional_string(kb, head, "lang");
        if fact_lang.as_deref() != Some(lang) {
            continue;
        }
        let Some(host_type) = named_string(kb, head, "host_type") else {
            continue;
        };
        let lift = named_optional_string(kb, head, "lift");
        let lower = named_optional_string(kb, head, "lower");
        let key = named_optional_string(kb, head, "key");
        hits.push(TypeMappingHit { host_type, lift, lower, key });
    }
    hits
}

/// WI-089(a): resolve the single best `TypeMapping` entry for an anthill type
/// under the active-key priority. `names` are the candidate `anthill_type`
/// spellings tried in order and merged (a sort's qualified name first, then its
/// short name, so a fact written either way resolves); `binding` is the
/// foreign-binding key in scope (`Some` at a carrier-dispatch boundary, `None`
/// in declared-signature position). Returns the highest-priority overlay
/// present — the one query that backs BOTH declared-signature host-type
/// lowering (reads `host_type`) and value-level marshalling (reads `lift` /
/// `lower`).
fn resolve_type_mapping(
    ctx: &CodegenContext,
    names: &[&str],
    binding: Option<&str>,
) -> Option<TypeMappingHit> {
    let mut hits: Vec<TypeMappingHit> = Vec::new();
    let mut queried: Vec<&str> = Vec::with_capacity(names.len());
    for name in names {
        // Skip a repeated spelling (a sort whose qualified name equals its short
        // name — builtins like `Float`) so we don't issue the same discrim query
        // twice and double the hit list.
        if queried.contains(name) {
            continue;
        }
        queried.push(name);
        hits.extend(query_type_mappings(ctx.kb, "cpp", name));
    }
    select_keyed(hits, |h| &h.key, &ctx.active_keys(binding))
}

/// WI-089: the C++ language-base host type for `anthill_type` (the
/// `key = none` entry), via the keyed `TypeMapping` query. Replaces the
/// hardcoded `prim_lower` / `param_lower` tables: primitives lower to a
/// leaf type (`Int64 -> int64_t`); parameterized stdlib sorts lower to a
/// bare template name (`List -> std::vector`) the caller wraps with args.
pub fn cpp_base_host_type(kb: &KnowledgeBase, anthill_type: &str) -> Option<String> {
    // The language base is the `key = none` entry — i.e. selection over the
    // single-element active-key list `[none]`. Routed through the shared
    // `select_keyed` so there is one implementation of "pick the entry for this
    // key", not a divergent `.find(key.is_none())` spelling.
    select_keyed(query_type_mappings(kb, "cpp", anthill_type), |h| &h.key, &[None])
        .map(|h| h.host_type)
}

/// WI-089(a): resolve the cpp host type for `anthill_type` under the active-key
/// priority — binding overlay > profile overlay > language base. `profile` is
/// the compilation profile (e.g. "cpp20-stl"); `binding` is the foreign-binding
/// key in scope at a carrier-dispatch boundary (`None` in declared-signature
/// position). This is exactly the selection declared-signature lowering and the
/// marshalling boundary perform internally, surfaced so callers (and tests) can
/// ask "what does X map to under profile P at binding B?". `cpp_base_host_type`
/// is the `profile = None, binding = None` special case.
pub fn cpp_host_type(
    kb: &KnowledgeBase,
    anthill_type: &str,
    profile: Option<&str>,
    binding: Option<&str>,
) -> Option<String> {
    let ctx = CodegenContext::with_profile(kb, profile.map(str::to_string));
    resolve_type_mapping(&ctx, &[anthill_type], binding).map(|h| h.host_type)
}

struct EffectMappingHit {
    /// Short name of the `ReceiverForm` variant (e.g. `"ResultWrap"`, `"MutRef"`).
    receiver: String,
    key: Option<String>,
}

/// WI-089(b): resolve cpp effect realizations over the keyed `EffectMapping`
/// facts — the effect_map analogue of `query_type_mappings`. Grounds `effect`
/// and post-filters the `Option` `lang`; `receiver` is read as the short name of
/// its `ReceiverForm` constructor.
fn query_effect_mappings(kb: &KnowledgeBase, lang: &str, effect: &str) -> Vec<EffectMappingHit> {
    const FIELDS: &[&str] = &["effect", "receiver", "lang", "key"];
    let mut hits = Vec::new();
    for head in query_realization_facts(
        kb,
        &["anthill.realization.EffectMapping", "EffectMapping"],
        FIELDS,
        "effect",
        effect,
    ) {
        let fact_lang = named_optional_string(kb, head, "lang");
        if fact_lang.as_deref() != Some(lang) {
            continue;
        }
        let Some(receiver) = named_arg(kb, head, "receiver").and_then(|t| functor_or_ref_short(kb, t))
        else {
            continue;
        };
        let key = named_optional_string(kb, head, "key");
        hits.push(EffectMappingHit { receiver, key });
    }
    hits
}

/// WI-089(b): the cpp `ReceiverForm` short name realizing `effect` under the
/// active-key priority — the effect_map analogue of declared-signature
/// host-type resolution. Effects are profile-scoped (not binding-scoped), so
/// the active keys are `[profile?, none]` (no binding prepended); a profile
/// overlay shadows the language base. `None` means no active key realizes the
/// effect — i.e. it is outside the profile's supported-effect set (the gate
/// WI-571 reads).
fn cpp_effect_receiver(ctx: &CodegenContext, effect: &str) -> Option<String> {
    let hits = query_effect_mappings(ctx.kb, "cpp", effect);
    select_keyed(hits, |h| &h.key, &ctx.active_keys(None)).map(|h| h.receiver)
}

/// WI-089(c): the cpp `(host_type spelling, include directive)` probe pairs from
/// the keyed `IncludeMapping` facts — replaces the hardcoded `INCLUDE_PROBES`
/// array. Grounds the plain-String `lang`; caller sorts for deterministic output.
fn query_include_mappings(kb: &KnowledgeBase, lang: &str) -> Vec<(String, String)> {
    let mut out = Vec::new();
    for head in query_realization_facts(
        kb,
        &["anthill.realization.IncludeMapping", "IncludeMapping"],
        &["host_type", "include", "lang"],
        "lang",
        lang,
    ) {
        let (Some(host_type), Some(include)) =
            (named_string(kb, head, "host_type"), named_string(kb, head, "include"))
        else {
            continue;
        };
        out.push((host_type, include));
    }
    out
}

/// WI-089(b): the host method spelling for an anthill `source` identifier under
/// the cpp `NamingConvention` fact — replaces the hardcoded `snake_to_camel`
/// call at the carrier-dispatch boundary. The only transform implemented is
/// `snake_case -> camelCase` (cpp's declared convention); any other declared
/// pair (including same-case) is identity, and an absent fact is identity too —
/// the convention is the source of truth, not a hardcoded default. Per-operation
/// acronym irregulars (get_gps -> getGPS) ride a CppName override (WI-087), not
/// this default.
fn cpp_method_name(kb: &KnowledgeBase, source: &str) -> String {
    let facts = query_realization_facts(
        kb,
        &["anthill.realization.NamingConvention", "NamingConvention"],
        &["language", "method_case", "source_case"],
        "language",
        "cpp",
    );
    // cpp_std supplies exactly one cpp NamingConvention. Zero (misconfigured or
    // unloaded profile) or >1 (an ambiguous overlay — per-op irregulars ride a
    // CppName attribute (WI-087), not a second fact) is a profile bug: fail
    // loudly in dev/test per the repo's "loud error over silent skip", degrade
    // to identity in release rather than abort codegen.
    debug_assert!(
        facts.len() == 1,
        "expected exactly one cpp NamingConvention fact, found {}",
        facts.len()
    );
    let Some(head) = facts.first().copied() else {
        return source.to_string();
    };
    match (
        named_string(kb, head, "source_case").as_deref(),
        named_string(kb, head, "method_case").as_deref(),
    ) {
        (Some("snake_case"), Some("camelCase")) => snake_to_camel(source),
        // Same source/method spelling: a legitimate no-op (e.g. a profile that
        // keeps snake_case).
        (Some(s), Some(m)) if s == m => source.to_string(),
        // A declared-but-unimplemented, partial, or malformed casing is a
        // realization-profile misconfiguration (not user input): fail loudly in
        // dev/test per "loud error over silent skip"; identity in release.
        (s, m) => {
            debug_assert!(
                false,
                "cpp NamingConvention declares unsupported/partial casing {s:?} -> {m:?}; \
                 only snake_case -> camelCase (and identity) are implemented"
            );
            source.to_string()
        }
    }
}
