// ── Carrier table & codegen context ──────────────────────────────────

/// Resolved table of qualified-anthill-sort-name → C++ host type,
/// built from `Implementation` + `CarrierBinding` facts in the KB.
///
/// Currently captures the *self-carrier* of each Implementation: when
/// `Implementation(target: "X", carrier: [CarrierBinding(sort_name: "X_short", host_type: "Y")])`
/// is asserted, `lookup("X")` returns `Some("Y")`. Sub-sort carriers
/// (CarrierBindings whose sort_name is not the target's short name)
/// are not yet wired through — extending this is mechanical when a
/// concrete consumer needs it.
///
/// Only Implementation facts with `language: "cpp"` participate.
pub struct CarrierTable {
    /// Anthill qualified name → host C++ type spelling.
    by_qualified: HashMap<String, String>,
    /// Anthill qualified name → header path that declares the host
    /// type (`Implementation.artifact`). Populated alongside the host
    /// type so the namespace-header writer can emit a corresponding
    /// `#include` whenever a carrier-bound type is referenced.
    artifacts: HashMap<String, String>,
    /// WI-089(a): anthill qualified name → foreign-binding overlay key
    /// (`Implementation.binding`, e.g. "webots"). A carrier-dispatch body
    /// onto this sort prepends the binding key to the active-key list so the
    /// binding's marshalling `TypeMapping` overlays win over the language
    /// base. Absent for a plain host-std realization (`binding: none`).
    bindings: HashMap<String, String>,
}

impl CarrierTable {
    /// Build the table by scanning the KB's `Implementation` facts.
    /// Returns an empty table if no `Implementation` symbol is in the
    /// KB (i.e. the realization stdlib wasn't loaded).
    pub fn from_kb(kb: &KnowledgeBase) -> Self {
        let mut by_qualified = HashMap::new();
        let mut artifacts = HashMap::new();
        let mut bindings = HashMap::new();

        let impl_sym = kb
            .try_resolve_symbol("anthill.realization.Implementation")
            .or_else(|| kb.try_resolve_symbol("Implementation"));
        let Some(impl_sym) = impl_sym else {
            return Self { by_qualified, artifacts, bindings };
        };

        for rid in kb.rules_by_functor(impl_sym) {
            let head = kb.rule_head(rid);

            let target = match named_string(kb, head, "target") {
                Some(t) => t,
                None => continue,
            };
            let language = named_string(kb, head, "language");
            if language.as_deref() != Some("cpp") {
                continue;
            }
            let artifact = named_string(kb, head, "artifact");
            // WI-089(a): the foreign-binding overlay key for this realization
            // (e.g. "webots"). `binding: none` (or an omitted field, padded to
            // an unbound var by the loader) reads back as None — a plain host-
            // std realization contributing no value-level marshalling.
            let impl_binding = named_optional_string(kb, head, "binding");

            let carrier_list = match named_arg(kb, head, "carrier") {
                Some(t) => t,
                None => continue,
            };
            let target_short = target
                .rsplit('.')
                .next()
                .unwrap_or(&target)
                .to_string();

            for binding in walk_list(kb, carrier_list) {
                let sort_name = named_string(kb, binding, "sort_name");
                let host_type = named_string(kb, binding, "host_type");
                if let (Some(sn), Some(ht)) = (sort_name, host_type) {
                    // For now: only the self-carrier (sort_name == target's
                    // short name) lands in the table keyed by `target`.
                    // Sub-sort carriers will get their own keys when needed.
                    if sn == target_short {
                        by_qualified.insert(target.clone(), ht);
                        // The loader defaults an absent `artifact` clause to
                        // "" (load.rs `unwrap_or_default`), so guard against
                        // empty here — otherwise a carrier with no artifact
                        // (e.g. `int64_t`, whose `<cstdint>` include comes
                        // from the std-include table) would emit `#include <>`.
                        if let Some(a) = artifact.clone().filter(|s| !s.is_empty()) {
                            artifacts.insert(target.clone(), a);
                        }
                        // WI-089(a): record the realization's binding key
                        // against the carrier sort, so a dispatch boundary onto
                        // it knows which marshalling overlay family is active.
                        if let Some(b) = impl_binding.clone() {
                            bindings.insert(target.clone(), b);
                        }
                    }
                }
            }
        }

        Self { by_qualified, artifacts, bindings }
    }

    /// Look up a host type by the anthill sort's fully qualified name.
    pub fn lookup(&self, qualified: &str) -> Option<&str> {
        self.by_qualified.get(qualified).map(|s| s.as_str())
    }

    /// Look up the header that declares this carrier's host type, if
    /// the source `Implementation` fact carried an `artifact`. Used
    /// to emit `#include "<artifact>"` in headers that reference the
    /// carrier-bound type.
    pub fn artifact(&self, qualified: &str) -> Option<&str> {
        self.artifacts.get(qualified).map(|s| s.as_str())
    }

    /// WI-089(a): the foreign-binding overlay key for this carrier sort, if its
    /// `Implementation` declared one (`binding: some("webots")`). A carrier-
    /// dispatch body onto this sort prepends the key to the active-key list so
    /// the binding's marshalling `TypeMapping` overlays shadow the language base.
    pub fn binding(&self, qualified: &str) -> Option<&str> {
        self.bindings.get(qualified).map(|s| s.as_str())
    }

    /// Whether the table has any entries (used by tests).
    pub fn is_empty(&self) -> bool {
        self.by_qualified.is_empty()
    }
}

/// A marshalled type representation: the value-level adapter pair that
/// bridges an anthill type and a foreign host representation of it.
struct Marshal {
    /// foreign->anthill adapter, applied to a value READ from the host
    /// API (e.g. a carrier-method return: `const double * -> Vec3`).
    lift: Option<String>,
    /// anthill->foreign adapter, applied to a value PASSED INTO a
    /// host-API call (e.g. a carrier-method arg: `Vec3 -> const double *`).
    lower: Option<String>,
}

// WI-089(a): marshalling is NO LONGER a separate prebuilt reader. The
// adapter-bearing `TypeMapping` overlays are resolved through the SAME keyed
// query as the plain renames (`resolve_type_mapping` / `query_type_mappings`),
// selected by the active-key priority — so there is one TypeMapping reader, one
// place that implements binding-shadows-profile-shadows-base. The former
// `MarshalTable` (a second functor scan that ignored the key) is retired; see
// `marshal_for_type`.

/// Resolved table of operation symbol → body TermId, built from
/// `anthill.realization.OperationImpl` facts. Operations with
/// expression bodies (e.g. `operation square(x: Int) -> Int = mul(x, x)`)
/// land here; the body Term is lowered to a C++ expression by
/// `lower_node`. Post-WI-249 the value is the operation body's
/// NodeOccurrence root sourced from `kb.op_body_node` — the legacy
/// `OperationImpl.body` Handle field is no longer read here. The
/// OperationImpl fact still exists (it's also a realization-side
/// declaration), but the body is materialized at load time.
pub struct OpImplTable {
    by_op: HashMap<Symbol, std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>>,
}

impl OpImplTable {
    pub fn from_kb(kb: &KnowledgeBase) -> Self {
        let mut by_op = HashMap::new();
        let sym = kb.try_resolve_symbol("anthill.realization.OperationImpl")
            .or_else(|| kb.try_resolve_symbol("OperationImpl"));
        let Some(sym) = sym else { return Self { by_op } };

        for rid in kb.rules_by_functor(sym) {
            let head = kb.rule_head(rid);
            // operation: Term::Ref(<op_sym>)
            let Some(op_term) = named_arg(kb, head, "operation") else { continue };
            let op_sym = match kb.get_term(op_term) {
                Term::Ref(s) | Term::Ident(s) => *s,
                _ => continue,
            };
            // Read the materialized body NodeOccurrence from the KB.
            // Ops with no body (spec-level declarations) are skipped.
            if let Some(node) = kb.op_body_node(op_sym) {
                by_op.insert(op_sym, node.clone());
            }
        }
        Self { by_op }
    }

    pub fn lookup(&self, op: Symbol)
        -> Option<std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>>
    {
        self.by_op.get(&op).cloned()
    }
}

/// Bundles the KB with a derived `CarrierTable` and `OpImplTable` so emit
/// functions don't have to re-scan facts on every lookup. Construct once per
/// codegen run; pass to every emit function. (Type/effect mappings are not
/// prebuilt — they ride the keyed-`TypeMapping` query, WI-089.)
pub struct CodegenContext<'kb> {
    pub kb: &'kb KnowledgeBase,
    pub carriers: CarrierTable,
    pub op_impls: OpImplTable,
    /// WI-089(a): the active compilation profile (e.g. "cpp20-stl"), threaded
    /// from the CLI (a `Generated`/`Implementation` fact's `profile`). Selects
    /// profile-keyed `TypeMapping` / `EffectMapping` overlays ahead of the
    /// language base in the active-key list. `None` = language base only.
    pub profile: Option<String>,
    /// Extra `#include` lines that lowering decides it needs at
    /// run-time (e.g. `<tl/expected.hpp>` for Error-effect ops). Merged
    /// into the rendered include set when emitting a header.
    pub requested_includes: std::cell::RefCell<std::collections::BTreeSet<String>>,
    /// Lexical stack of in-scope type parameters. Each frame holds
    /// the params introduced by one enclosing sort; lookup walks from
    /// top to bottom so an inner sort still sees an outer sort's
    /// params, while an inner declaration of the same name shadows
    /// the outer for the duration of inner emission.
    pub type_params: std::cell::RefCell<Vec<std::collections::HashMap<String, String>>>,
    /// Lexical stack of in-scope value bindings introduced by match
    /// patterns. Each frame maps a source-level binding name to the
    /// C++ access expression that retrieves the bound value (e.g.
    /// `"w" → "o.value()"` inside `case some(?w) ->`). Lookup walks
    /// top-down so nested matches shadow outer bindings.
    pub value_bindings: std::cell::RefCell<Vec<std::collections::HashMap<String, String>>>,
    /// Anthill namespace currently being emitted as a C++ namespace
    /// block. Set by `emit_namespace_header_in`. Used by `sort_to_cpp`
    /// to decide between short-name (`Vec3`) and fully-qualified
    /// (`::anthill::geometry::Vec3`) references for cross-namespace
    /// entity types.
    pub emitting_namespace: std::cell::RefCell<Option<String>>,
}

impl<'kb> CodegenContext<'kb> {
    /// Build a context with no active profile (language base only). Most
    /// callers; the profile-aware overlays are forward-looking.
    pub fn new(kb: &'kb KnowledgeBase) -> Self {
        Self::with_profile(kb, None)
    }

    /// WI-089(a): build a context for a specific compilation profile, so
    /// profile-keyed `TypeMapping` / `EffectMapping` overlays (e.g. a
    /// cpp20-stl-only rename) are selected ahead of the language base.
    pub fn with_profile(kb: &'kb KnowledgeBase, profile: Option<String>) -> Self {
        Self {
            kb,
            carriers: CarrierTable::from_kb(kb),
            op_impls: OpImplTable::from_kb(kb),
            profile,
            requested_includes: std::cell::RefCell::new(std::collections::BTreeSet::new()),
            type_params: std::cell::RefCell::new(Vec::new()),
            value_bindings: std::cell::RefCell::new(Vec::new()),
            emitting_namespace: std::cell::RefCell::new(None),
        }
    }

    /// WI-089(a): the ordered active-key list for a keyed realization-fact
    /// query, most-specific-first. `binding` is the foreign-binding key in
    /// scope (`Some` at a carrier-dispatch boundary, `None` in declared-
    /// signature position); the compilation profile follows; the language base
    /// (`None`) is always last. `select_keyed` walks this list and the first
    /// key with a matching fact wins — so a binding overlay shadows a profile
    /// overlay shadows the base. The base sentinel is always present, so a type
    /// with only a base entry still resolves.
    fn active_keys(&self, binding: Option<&str>) -> Vec<Option<String>> {
        let mut keys = Vec::with_capacity(3);
        if let Some(b) = binding {
            keys.push(Some(b.to_string()));
        }
        if let Some(p) = &self.profile {
            keys.push(Some(p.clone()));
        }
        keys.push(None);
        keys
    }
}

impl<'kb> CodegenContext<'kb> {
    /// Look up a type-parameter binding by source-level name. Walks
    /// the lexical stack top-down so inner declarations shadow outer
    /// ones with the same name.
    pub fn lookup_type_param(&self, name: &str) -> Option<String> {
        let stack = self.type_params.borrow();
        for frame in stack.iter().rev() {
            if let Some(v) = frame.get(name) {
                return Some(v.clone());
            }
        }
        None
    }

    /// Push a fresh frame of type-param bindings onto the lexical
    /// stack. Returns a guard that pops the frame on drop, so the
    /// caller doesn't have to remember to restore by hand.
    pub fn push_type_params(&self, frame: std::collections::HashMap<String, String>) -> TypeParamGuard<'_, 'kb> {
        self.type_params.borrow_mut().push(frame);
        TypeParamGuard { ctx: self }
    }

    /// Look up a value binding (introduced by a match pattern, e.g.
    /// `?w` from `case some(?w) -> ...`) and return its C++ access
    /// expression. Walks the lexical stack top-down for shadowing.
    pub fn lookup_value_binding(&self, name: &str) -> Option<String> {
        let stack = self.value_bindings.borrow();
        for frame in stack.iter().rev() {
            if let Some(v) = frame.get(name) {
                return Some(v.clone());
            }
        }
        None
    }

    /// Push a fresh frame of value bindings. Returns a RAII guard
    /// that pops on drop so callers don't need to manage the stack
    /// by hand. Used by match-branch lowering.
    pub fn push_value_bindings(&self, frame: std::collections::HashMap<String, String>) -> ValueBindingGuard<'_, 'kb> {
        self.value_bindings.borrow_mut().push(frame);
        ValueBindingGuard { ctx: self }
    }

    /// Set the namespace currently being emitted as a C++ namespace
    /// block. Returns a RAII guard that restores the previous value
    /// on drop, so early-returns through `?` don't leave the cell
    /// in the wrong state. Read by `qualify_cross_namespace` and
    /// `register_cross_namespace_include`.
    pub fn enter_namespace(&self, namespace: &str) -> NamespaceGuard<'_, 'kb> {
        let prev = self.emitting_namespace.borrow_mut().replace(namespace.to_string());
        NamespaceGuard { ctx: self, prev }
    }
}

/// RAII guard that pops the top type-param frame on drop. Constructed
/// by `CodegenContext::push_type_params`; the lifetime ties it to the
/// context so nested calls compose naturally.
pub struct TypeParamGuard<'g, 'kb> {
    ctx: &'g CodegenContext<'kb>,
}

impl<'g, 'kb> Drop for TypeParamGuard<'g, 'kb> {
    fn drop(&mut self) {
        self.ctx.type_params.borrow_mut().pop();
    }
}

/// RAII guard that pops the top value-binding frame on drop.
/// Constructed by `CodegenContext::push_value_bindings`.
pub struct ValueBindingGuard<'g, 'kb> {
    ctx: &'g CodegenContext<'kb>,
}

impl<'g, 'kb> Drop for ValueBindingGuard<'g, 'kb> {
    fn drop(&mut self) {
        self.ctx.value_bindings.borrow_mut().pop();
    }
}

/// RAII guard that restores `emitting_namespace` on drop. Constructed
/// by `CodegenContext::enter_namespace`; `prev` holds whatever the
/// cell contained before this guard was created (typically `None`
/// for top-level emission, `Some(outer)` for a nested namespace).
pub struct NamespaceGuard<'g, 'kb> {
    ctx: &'g CodegenContext<'kb>,
    prev: Option<String>,
}

impl<'g, 'kb> Drop for NamespaceGuard<'g, 'kb> {
    fn drop(&mut self) {
        *self.ctx.emitting_namespace.borrow_mut() = self.prev.take();
    }
}

// ── Term inspection helpers ──────────────────────────────────────────

/// Get the value of a named argument from a `Term::Fn` head.
fn named_arg(kb: &KnowledgeBase, head: TermId, name: &str) -> Option<TermId> {
    if let Term::Fn { named_args, .. } = kb.get_term(head) {
        for (sym, val) in named_args {
            if kb.resolve_sym(*sym) == name {
                return Some(*val);
            }
        }
    }
    None
}

/// Pull a `Literal::String` out of a term, if that's what it is.
fn as_string(kb: &KnowledgeBase, term: TermId) -> Option<String> {
    if let Term::Const(Literal::String(s)) = kb.get_term(term) {
        Some(s.clone())
    } else {
        None
    }
}

/// Combination of `named_arg` + `as_string`.
fn named_string(kb: &KnowledgeBase, head: TermId, name: &str) -> Option<String> {
    named_arg(kb, head, name).and_then(|t| as_string(kb, t))
}

/// Read a named arg declared `Option[String]`, returning the inner string when
/// present (`some("x")` -> `Some("x")`, `none` -> `None`). The Option-typed
/// counterpart of `named_string` (sibling reader for keyed realization facts).
fn named_optional_string(kb: &KnowledgeBase, head: TermId, name: &str) -> Option<String> {
    named_arg(kb, head, name).and_then(|t| extract_optional_string(kb, t))
}

/// Return the qualified name of `sym`'s parent, if any — e.g.
/// `test.geom.Vec3.x` → `Some("test.geom.Vec3")`. Used in lieu of
/// `SymbolTable::scope_of`, which has buggy semantics (finds a
/// sibling in the same scope, not the parent).
fn parent_qualified_name(kb: &KnowledgeBase, sym: Symbol) -> Option<String> {
    parent_namespace_of(kb.qualified_name_of(sym)).map(str::to_string)
}

/// Drop the trailing `.<short>` from a dotted qualified name.
/// `"test.geom.Vec3.x"` → `Some("test.geom.Vec3")`; `"top"` → `None`.
fn parent_namespace_of(qn: &str) -> Option<&str> {
    qn.rsplit_once('.').map(|(parent, _)| parent)
}

/// Last `.`-separated component of a qualified name. `test.geom.Vec3` → `"Vec3"`.
fn short_name_of(qn: &str) -> &str {
    qn.rsplit('.').next().unwrap_or(qn)
}

/// Filename convention for a generated namespace's header: dotted
/// name with `.` → `_`. Mirrors what `anthill codegen cpp-project`
/// writes (`anthill.geometry` → `anthill_geometry.hpp`); both
/// producers and consumers of cross-namespace references must agree.
pub fn header_filename_for_namespace(namespace: &str) -> String {
    format!("{}.hpp", namespace.replace('.', "_"))
}

/// Register an `#include "<other_namespace>.hpp"` when emitting a
/// reference to an entity in another anthill namespace. The
/// project-layout writer drops these files alongside the current
/// namespace's header, so the include resolves at compile time. No-op
/// when the reference is intra-namespace or no surrounding namespace
/// context is set.
fn register_cross_namespace_include(ctx: &CodegenContext, entity_qn: &str) {
    let current_ns = ctx.emitting_namespace.borrow();
    let Some(current) = current_ns.as_deref() else { return };
    let Some(entity_ns) = parent_namespace_of(entity_qn) else { return };
    if entity_ns == current { return; }
    let header = header_filename_for_namespace(entity_ns);
    ctx.requested_includes.borrow_mut()
        .insert(format!("#include \"{header}\""));
}

/// If a namespace block is being emitted and `entity_qn` belongs to
/// a *different* anthill namespace, return the C++ fully-qualified
/// path (`::anthill::geometry::Vec3`) so the reference compiles
/// inside the current `namespace foo::bar { ... }` block. Same
/// namespace, or no namespace context (direct `emit_traits_struct`
/// callers), fall back to the short name.
fn qualify_cross_namespace(ctx: &CodegenContext, entity_qn: &str, short: &str) -> String {
    let current_ns = ctx.emitting_namespace.borrow();
    let Some(current) = current_ns.as_deref() else { return short.to_string() };
    let entity_ns = parent_namespace_of(entity_qn);
    match entity_ns {
        Some(ens) if ens == current => short.to_string(),
        Some(_) => format!("::{}", entity_qn.replace('.', "::")),
        None => short.to_string(),
    }
}

/// Walk a list-shaped term and collect element TermIds. Recognises:
///   - `ListLiteral(a, b, ...)` — pre-typing parser output
///   - `cons(head: ..., tail: ...)` — typed list form using named args
///   - `cons(head, tail)` — typed list form using positional args
///   - `nil` — list terminator
///
/// Anything else returns whatever has been collected so far.
fn walk_list(kb: &KnowledgeBase, list: TermId) -> Vec<TermId> {
    let mut out = Vec::new();
    let mut current = list;
    loop {
        let term = kb.get_term(current);
        let Term::Fn { functor, pos_args, named_args } = term else { return out };
        match kb.resolve_sym(*functor) {
            "ListLiteral" => {
                for arg in pos_args {
                    out.push(*arg);
                }
                return out;
            }
            "cons" => {
                // Try named args first (head:/tail:), fall back to positional.
                let head = named_args
                    .iter()
                    .find(|(s, _)| kb.resolve_sym(*s) == "head")
                    .map(|(_, v)| *v)
                    .or_else(|| pos_args.first().copied());
                let tail = named_args
                    .iter()
                    .find(|(s, _)| kb.resolve_sym(*s) == "tail")
                    .map(|(_, v)| *v)
                    .or_else(|| pos_args.get(1).copied());
                match (head, tail) {
                    (Some(h), Some(t)) => {
                        out.push(h);
                        current = t;
                    }
                    _ => return out,
                }
            }
            "nil" => return out,
            _ => return out,
        }
    }
}

// ── Public API ───────────────────────────────────────────────────────

/// Emit a C++ struct for a single anthill entity, looked up by name.
/// The name may be either short ("Vec3") or fully qualified
/// ("test.geom.Vec3"); the C++ struct is named with the short
/// component, regardless of which form was passed in.
///
/// Convenience wrapper: builds a fresh `CodegenContext` from `kb`
/// (which scans `Implementation` facts). For repeated calls in one
/// codegen run, build the context once and use the by-symbol API.
pub fn emit_entity_struct(
    kb: &KnowledgeBase,
    entity_name: &str,
) -> Result<String, CppCodegenError> {
    let ctx = CodegenContext::new(kb);
    let sym = kb.try_resolve_symbol(entity_name).ok_or_else(|| {
        CppCodegenError {
            message: format!("entity '{entity_name}' not found in KB"),
        }
    })?;
    emit_entity_struct_by_symbol(&ctx, sym)
}

/// Emit a C++ struct for an entity given its functor Symbol.
/// The struct's C++ name is the short (final) component of the
/// resolved qualified name, so callers cannot accidentally inject
/// a dotted path as the struct identifier.
pub fn emit_entity_struct_by_symbol(
    ctx: &CodegenContext,
    functor: Symbol,
) -> Result<String, CppCodegenError> {
    let kb = ctx.kb;
    let qualified = kb.qualified_name_of(functor);
    let display_name = short_name_of(qualified);

    let fields = kb.entity_field_types(functor).ok_or_else(|| {
        CppCodegenError {
            message: format!(
                "'{qualified}' has no registered fields — is it really an entity?"
            ),
        }
    })?;

    // Templates: an entity declared inside `sort S { sort T = ?; … }`
    // takes its parent sort's type parameters. We emit the template
    // prefix and push a frame onto the lexical scope stack for the
    // duration of field-type lowering — outer params (if any) stay
    // visible underneath.
    let parent_sym = parent_qualified_name(kb, functor)
        .and_then(|qn| kb.try_resolve_symbol(&qn));
    let (template, type_params) = match parent_sym {
        Some(p) => template_prefix_for_sort(kb, p),
        None => (String::new(), std::collections::HashMap::new()),
    };
    let _guard = ctx.push_type_params(type_params);

    let mut fields_text = String::new();
    for (field_sym, type_term) in fields {
        // WI-342: field types are carrier-agnostic; codegen handles only ground
        // types (a value-in-type / denoted field is not C++-representable and
        // does not occur in codegen'd entities).
        let Value::Term(type_tid) = type_term else { continue };
        let cpp_type = lower_type(ctx, *type_tid)?;
        let field_name = kb.resolve_sym(*field_sym);
        fields_text.push_str(
            &TEMPLATE_FIELD
                .replace("{ty}", &cpp_type)
                .replace("{name}", field_name),
        );
    }

    Ok(TEMPLATE_ENTITY_STRUCT
        .replace("{template}", &template)
        .replace("{name}", display_name)
        .replace("{fields}", &fields_text))
}

// ── Traits-class (sort-with-operations) emission ────────────────────

/// Emit a C++ "traits-class" struct exposing every operation of a
/// sort as a `static` method declaration. Convenience wrapper that
/// builds a fresh `CodegenContext`.
///
/// Today emits declarations only — no method bodies. Body generation
/// (calling into the carrier with `self->method(args)`) needs a
/// snake-to-camel naming convention which is part of WI-088 / WI-089.
/// Until then, the user fills bodies manually or hand-writes the
/// implementation file.
/// One term-level constant (proposal 039 / WI-084), lowered for C++ emission
/// as a `constexpr`. WI-533.
struct ConstSig {
    name: String,
    cpp_type: String,
    cpp_value: String,
}

/// Discover and lower the term-level constants declared directly in `scope`
/// (a sort's or a namespace's qualified name). Consts carry no scope-member
/// fact (the WI-084 deferral), so they are absent from the fact index and
/// found instead by scanning `const_types_iter` and matching the parent
/// qualified name — mirroring how `operations_in_sort` filters operations by
/// `parent_qualified_name`. Sorted by qualified name for deterministic output.
/// A const whose declared type or value can't be lowered is a loud error, never
/// a silent drop (repo rule).
fn consts_in_scope(
    ctx: &CodegenContext,
    scope_qualified: &str,
) -> Result<Vec<ConstSig>, CppCodegenError> {
    let kb = ctx.kb;
    let mut syms: Vec<Symbol> = kb
        .const_types_iter()
        .filter(|(sym, _)| parent_qualified_name(kb, *sym).as_deref() == Some(scope_qualified))
        .map(|(sym, _)| sym)
        .collect();
    syms.sort_by(|a, b| kb.qualified_name_of(*a).cmp(kb.qualified_name_of(*b)));

    let mut out = Vec::with_capacity(syms.len());
    for sym in syms {
        let name = short_name_of(kb.qualified_name_of(sym)).to_string();
        let (cpp_type, cpp_value) = lower_one_const(ctx, sym)?;
        out.push(ConstSig { name, cpp_type, cpp_value });
    }
    Ok(out)
}

/// Lower a single const's declared type and value to C++. Shared by
/// `consts_in_scope` (struct/namespace members) and
/// `carrier_bound_const_companions` (WI-536 namespace companions).
fn lower_one_const(ctx: &CodegenContext, sym: Symbol) -> Result<(String, String), CppCodegenError> {
    let kb = ctx.kb;
    let qn = kb.qualified_name_of(sym).to_string();

    // Declared type: `const_type` is a `Value`; a simple sort type (the
    // common case — `Int64`, `Float`) lowers as a ground `Value::Term`.
    let cpp_type = match kb.const_type(sym) {
        Some(anthill_core::eval::value::Value::Term(tid)) => {
            let t = lower_type(ctx, *tid)?;
            // A `String` const cannot be `constexpr std::string` — std::string
            // is not a literal type before C++20. `std::string_view` is literal
            // and binds a string literal directly, so it works under C++17.
            if t == "std::string" { "std::string_view".to_string() } else { t }
        }
        Some(other) => {
            return Err(CppCodegenError {
                message: format!(
                    "const '{qn}': declared type is a non-ground value ({}) — \
                     value-in-type const types are not supported by cpp-gen yet",
                    other.type_name()
                ),
            });
        }
        None => {
            return Err(CppCodegenError {
                message: format!("const '{qn}': no declared type recorded in the KB"),
            });
        }
    };

    // Value: lower the anthill body when present; a bodyless host-supplied
    // const (WI-532) maps to its target expression (the Float IEEE specials).
    let cpp_value = match kb.const_body_node(sym) {
        Some(body) => lower_node(ctx, body)?,
        None => render_as_float_special(&qn).ok_or_else(|| CppCodegenError {
            message: format!(
                "const '{qn}': bodyless host-supplied const has no C++ value mapping \
                 (only the Float IEEE specials infinity/negativeInfinity/nan are mapped)"
            ),
        })?,
    };

    Ok((cpp_type, cpp_value))
}

/// WI-536: carrier-bound sorts are excluded from struct emission (their carrier
/// owns the type), so a const declared inside one has nowhere to live. Emit each
/// as a namespace-scope companion named `Sort_NAME` (non-shadowing) so a
/// reference can resolve to a named constant. Returns the companions for the
/// carrier-bound sorts declared one level under `namespace`, sorted by const
/// qualified name for determinism. `const_ref_cpp` produces the matching
/// reference form.
fn carrier_bound_const_companions(
    ctx: &CodegenContext,
    namespace: &str,
) -> Result<Vec<ConstSig>, CppCodegenError> {
    let kb = ctx.kb;
    let prefix = format!("{namespace}.");
    let mut syms: Vec<Symbol> = kb
        .const_types_iter()
        .filter(|(sym, _)| {
            let Some(parent) = parent_qualified_name(kb, *sym) else { return false };
            parent.starts_with(&prefix)
                && !parent[prefix.len()..].contains('.')
                && ctx.carriers.lookup(&parent).is_some()
                && matches!(
                    kb.try_resolve_symbol(&parent).and_then(|s| kb.kind_of(s)),
                    Some(SymbolKind::Sort)
                )
        })
        .map(|(sym, _)| sym)
        .collect();
    syms.sort_by(|a, b| kb.qualified_name_of(*a).cmp(kb.qualified_name_of(*b)));

    let mut out = Vec::with_capacity(syms.len());
    for sym in syms {
        let name = companion_name(kb, sym);
        let (cpp_type, cpp_value) = lower_one_const(ctx, sym)?;
        out.push(ConstSig { name, cpp_type, cpp_value });
    }
    Ok(out)
}

/// The namespace-scope companion identifier for a carrier-bound sort's const:
/// `Sort_NAME`. Single source of truth shared by the declaration site
/// (`carrier_bound_const_companions`) and the reference site (`const_ref_cpp`),
/// so the two cannot drift out of sync.
fn companion_name(kb: &KnowledgeBase, const_sym: Symbol) -> String {
    let qn = kb.qualified_name_of(const_sym);
    let parent = parent_qualified_name(kb, const_sym);
    let sort_short = parent.as_deref().map(short_name_of).unwrap_or("");
    format!("{sort_short}_{}", short_name_of(qn))
}

/// WI-536: C++ reference expression for a term-level const — a NAMED constant,
/// not the inlined value. A const in a carrier-bound sort uses the namespace
/// companion `Sort_NAME`; a const in an emitted (non-carrier) sort uses the
/// struct member `Sort::NAME`; a namespace-level const uses `NAME`. References
/// from another namespace are fully qualified and pull in the declaring
/// namespace's header.
fn const_ref_cpp(ctx: &CodegenContext, sym: Symbol) -> String {
    let kb = ctx.kb;
    let qn = kb.qualified_name_of(sym).to_string();
    let name = short_name_of(&qn);
    let Some(parent) = parent_qualified_name(kb, sym) else {
        return name.to_string();
    };
    let parent_is_sort = matches!(
        kb.try_resolve_symbol(&parent).and_then(|s| kb.kind_of(s)),
        Some(SymbolKind::Sort)
    );

    // (declaring namespace, identifier within that namespace)
    let (decl_ns, in_ns): (String, String) = if parent_is_sort {
        let sort_ns = parent_namespace_of(&parent).unwrap_or("").to_string();
        if ctx.carriers.lookup(&parent).is_some() {
            (sort_ns, companion_name(kb, sym)) // namespace companion
        } else {
            (sort_ns, format!("{}::{name}", short_name_of(&parent))) // emitted struct member
        }
    } else {
        // Namespace-level const: the parent qualified name IS the namespace.
        (parent, name.to_string())
    };

    let current = ctx.emitting_namespace.borrow().clone();
    match current {
        Some(cur) if cur == decl_ns => in_ns,
        _ => {
            if !decl_ns.is_empty() {
                let header = header_filename_for_namespace(&decl_ns);
                ctx.requested_includes
                    .borrow_mut()
                    .insert(format!("#include \"{header}\""));
                format!("::{}::{}", decl_ns.replace('.', "::"), in_ns)
            } else {
                format!("::{in_ns}")
            }
        }
    }
}

/// Order namespace-scope const-likes (WI-533 namespace consts + WI-536
/// carrier-bound companions) so a const whose lowered value references another
/// const in the same band is emitted AFTER it — a C++ `inline constexpr`
/// initializer must name only already-declared constants. Dependency is read
/// off the lowered value strings: const A depends on const B when B's emitted
/// identifier appears as a whole token in A's value. Ready nodes are taken in
/// the band's incoming (qualified-name) order for determinism. A dependency
/// cycle (a pair of consts referencing each other) cannot be a constexpr and is
/// a loud error. WI-536.
fn topo_sort_const_likes(band: Vec<ConstSig>) -> Result<Vec<ConstSig>, CppCodegenError> {
    let n = band.len();
    // deps[i] = indices j (j != i) whose emitted name appears in band[i]'s value.
    let deps: Vec<Vec<usize>> = (0..n)
        .map(|i| {
            (0..n)
                .filter(|&j| j != i && value_references_token(&band[i].cpp_value, &band[j].name))
                .collect()
        })
        .collect();

    let mut done = vec![false; n];
    let mut order: Vec<usize> = Vec::with_capacity(n);
    for _ in 0..n {
        // Smallest index whose dependencies are all already emitted.
        match (0..n).find(|&i| !done[i] && deps[i].iter().all(|&j| done[j])) {
            Some(i) => {
                done[i] = true;
                order.push(i);
            }
            None => {
                let cyclic: Vec<&str> =
                    (0..n).filter(|&i| !done[i]).map(|i| band[i].name.as_str()).collect();
                return Err(CppCodegenError {
                    message: format!(
                        "const dependency cycle among {cyclic:?} — a cyclic constant cannot be \
                         emitted as a C++ constexpr"
                    ),
                });
            }
        }
    }

    let mut slots: Vec<Option<ConstSig>> = band.into_iter().map(Some).collect();
    Ok(order.into_iter().map(|i| slots[i].take().expect("each index taken once")).collect())
}

/// True if `name` occurs in `haystack` as a whole C++ identifier token — not as
/// a substring of a larger identifier (so `BROADCAST` does not match inside
/// `BROADCAST_CHANNEL`, and `BROADCAST_CHANNEL` does not match inside
/// `Emitter_BROADCAST_CHANNEL`).
fn value_references_token(haystack: &str, name: &str) -> bool {
    if name.is_empty() {
        return false;
    }
    let bytes = haystack.as_bytes();
    let is_ident = |b: u8| b.is_ascii_alphanumeric() || b == b'_';
    let mut from = 0;
    while let Some(pos) = haystack[from..].find(name) {
        let start = from + pos;
        let end = start + name.len();
        let before_ok = start == 0 || !is_ident(bytes[start - 1]);
        let after_ok = end == bytes.len() || !is_ident(bytes[end]);
        if before_ok && after_ok {
            return true;
        }
        from = start + 1;
    }
    false
}

pub fn emit_traits_struct(
    kb: &KnowledgeBase,
    sort_name: &str,
) -> Result<String, CppCodegenError> {
    let ctx = CodegenContext::new(kb);
    let sym = kb.try_resolve_symbol(sort_name).ok_or_else(|| {
        CppCodegenError {
            message: format!("sort '{sort_name}' not found in KB"),
        }
    })?;
    emit_traits_struct_by_symbol(&ctx, sym)
}

/// Emit a traits-class struct for a sort given its functor Symbol.
/// Uses the sort's short (final) qualified-name component as the C++
/// struct name. Returns an error if the sort has no operations in
/// its scope.
pub fn emit_traits_struct_by_symbol(
    ctx: &CodegenContext,
    sort_sym: Symbol,
) -> Result<String, CppCodegenError> {
    let kb = ctx.kb;
    let qualified = kb.qualified_name_of(sort_sym);
    let display_name = short_name_of(qualified);

    // Push the sort's type params onto the lexical scope stack for
    // the duration of operation signature lowering — `lower_type`
    // consults the stack to render `?T` references to the C++
    // template parameter name.
    let (template, type_params) = template_prefix_for_sort(kb, sort_sym);
    let _guard = ctx.push_type_params(type_params);

    let ops = operations_in_sort(ctx, sort_sym)?;
    let consts = consts_in_scope(ctx, qualified)?;
    if ops.is_empty() && consts.is_empty() {
        return Err(CppCodegenError {
            message: format!(
                "sort '{qualified}' has no operations or constants — \
                 a traits struct would be empty"
            ),
        });
    }

    let mut methods_text = String::new();
    // Term-level constants (WI-533) first: they read as the class's named
    // sentinels and match source order where the const precedes the operations
    // (e.g. lf1 `Emitter::BROADCAST_CHANNEL`).
    for c in &consts {
        methods_text.push_str(&format!(
            "    static constexpr {} {} = {};\n",
            c.cpp_type, c.name, c.cpp_value
        ));
    }
    for op in &ops {
        let params_text = op.params.iter()
            .map(|p| format!("{} {}", p.cpp_type, p.name))
            .collect::<Vec<_>>()
            .join(", ");
        let rendered = match &op.body {
            Some(body) => TEMPLATE_STATIC_METHOD_BODY
                .replace("{ret}", &op.return_type_cpp)
                .replace("{name}", &op.name)
                .replace("{params}", &params_text)
                .replace("{body}", body),
            None => TEMPLATE_STATIC_METHOD_DECL
                .replace("{ret}", &op.return_type_cpp)
                .replace("{name}", &op.name)
                .replace("{params}", &params_text),
        };
        methods_text.push_str(&rendered);
    }

    Ok(TEMPLATE_TRAITS_STRUCT
        .replace("{template}", &template)
        .replace("{name}", display_name)
        .replace("{methods}", &methods_text))
}

/// One operation's signature, as needed for traits-class emission.
/// `body` is `Some` when we can synthesise a definition that calls
/// into the carrier; `None` otherwise (declaration-only).
struct OperationSig {
    name: String,
    params: Vec<ParamInfo>,
    return_type_cpp: String,
    body: Option<String>,
}

struct ParamInfo {
    name: String,
    cpp_type: String,
    /// The param's anthill type term, kept so body synthesis can look
    /// up a marshalled representation (WI-088) and `lower` the argument
    /// when the host API wants the foreign rep.
    type_term: TermId,
}

/// Whether an operation's return type is "body-emittable" — i.e.,
/// we can safely synthesise `return self->method(args);` without a
/// marshalling step.
///
/// True when the anthill return type is:
///   - a primitive (`Int`, `Float`, `Bool`, `String`, `Unit`)
///   - a carrier-bound sort (we know the C++ type matches the
///     method's return)
///   - a parameterized type whose base is body-emittable AND every
///     binding's value is body-emittable (e.g. `List[T = Float]`,
///     `Option[T = List[T = Bool]]`)
///
/// False for project-local entities (we don't know what the carrier
/// method returns — a Vec3-from-`const double *` shape needs WI-088
/// marshalling) and for anything else exotic.
fn is_body_emittable(ctx: &CodegenContext, type_term: TermId) -> bool {
    let kb = ctx.kb;
    if let Some(sym) = extract_sort_ref_sym(kb, &TermIdView(type_term)) {
        let qualified = kb.qualified_name_of(sym);
        if ctx.carriers.lookup(qualified).is_some() {
            return true;
        }
        let short = short_name_of(qualified);
        return resolve_type_mapping(ctx, &[short], None).is_some();
    }
    let Some((base_sym, binding_values)) = unpack_parameterized(kb, type_term) else {
        return false;
    };
    let base_qn = kb.qualified_name_of(base_sym);
    let base_short = short_name_of(base_qn);
    let base_known = ctx.carriers.lookup(base_qn).is_some()
        || resolve_type_mapping(ctx, &[base_short], None).is_some();
    if !base_known {
        return false;
    }
    binding_values.iter().all(|(_, v)| is_body_emittable(ctx, *v))
}

/// If `type_term` is a `parameterized(base: sort_ref(<sort>), bindings:
/// [TypeBinding(value: <type>) ...])` term, return the base sort
/// symbol and the list of binding-value term ids. Otherwise `None`.
/// Shared between `lower_parameterized` (which formats the C++ template
/// expression) and `is_body_emittable` (which decides whether a body
/// can be synthesised).
fn unpack_parameterized(kb: &KnowledgeBase, term: TermId) -> Option<(Symbol, Vec<(Symbol, TermId)>)> {
    // WI-361: read base + `(param, value)` bindings form-agnostically — deep
    // `parameterized(base: sort_ref(S), bindings)` or term-backed `Fn{S, named}`.
    // The param symbol is kept so `lower_parameterized` can order the template
    // args by the sort's DECLARATION order (the bindings are stored in canonical
    // symbol-interning order, NOT declaration order, post the WI-361 flip).
    let TypeExtractor::Parameterized { base, bindings } = extract_type(kb, &TermIdView(term)) else {
        return None;
    };
    // cpp-gen operates on well-formed ground parameterized types: every binding is
    // a `TypeBinding(param, value)` whose value is a hash-consed `TermId`. (A
    // non-`Term` value maps to `None` → whole result `None`; a binding missing
    // `value` is skipped by `extract_type` — both unreachable for loader types.)
    let values: Option<Vec<(Symbol, TermId)>> = bindings
        .iter()
        .map(|(p, v)| match v {
            Value::Term(t) => Some((*p, *t)),
            _ => None,
        })
        .collect();
    Some((base, values?))
}

/// Convert a snake_case identifier to camelCase. Naive transform —
/// drops underscores and uppercases the next char. Does not preserve
/// acronyms (`get_gps` → `getGps`, not `getGPS`). Per-operation
/// overrides will arrive with WI-087 attributes; until then,
/// acronym-heavy ops emit the wrong C++ method name and need a
/// hand-edit. Documented in the cpp-forward-mapping spec.
fn snake_to_camel(snake: &str) -> String {
    let mut out = String::with_capacity(snake.len());
    let mut upper_next = false;
    for c in snake.chars() {
        if c == '_' {
            upper_next = true;
        } else if upper_next {
            out.extend(c.to_uppercase());
            upper_next = false;
        } else {
            out.push(c);
        }
    }
    out
}

/// Look up the marshalled representation (WI-088) for an anthill type term at a
/// carrier-dispatch boundary, if one is registered. Resolves the term to its
/// sort symbol and runs the keyed `TypeMapping` query (WI-089(a)) under the
/// active binding key — so the binding's marshalling overlay (`Vec3 -> const
/// double *`) is selected here, while declared-signature lowering (with no
/// binding key active) never sees it. A hit that carries no adapter (`lift` and
/// `lower` both absent) is a plain rename, not a marshalled rep, so it returns
/// `None`: the value passes through bare.
fn marshal_for_type(ctx: &CodegenContext, type_term: TermId, binding: Option<&str>) -> Option<Marshal> {
    let kb = ctx.kb;
    let sym = extract_sort_ref_sym(kb, &TermIdView(type_term)).or_else(|| {
        match kb.get_term(type_term) {
            Term::Ref(s) | Term::Ident(s) => Some(*s),
            _ => None,
        }
    })?;
    let qualified = kb.qualified_name_of(sym);
    let short = short_name_of(qualified);
    let hit = resolve_type_mapping(ctx, &[qualified, short], binding)?;
    if hit.lift.is_none() && hit.lower.is_none() {
        return None;
    }
    Some(Marshal { lift: hit.lift, lower: hit.lower })
}

/// Synthesise a method body for an operation. Lookup order:
///   1. **OperationImpl** (anthill expression body) — lower the body
///      via `lower_expr` and wrap with `return`. Highest precedence:
///      an operation with an explicit body MUST use that body.
///   2. **Carrier dispatch with marshalling** (WI-088) — `self->method(args)`,
///      with each argument `lower`ed and the result `lift`ed when its
///      anthill type has a marshalled representation. The host method's
///      foreign signature is derived by mapping the operation's anthill
///      types through the `MarshalTable`; `lift`/`lower` adapters bridge
///      the boundary (`return Vec3::from_array(self->getValues());`).
///   3. **None** — fall back to a declaration-only signature.
fn synthesise_body_for(
    ctx: &CodegenContext,
    op_sym: Symbol,
    name: &str,
    params: &[ParamInfo],
    return_type: &str,
    return_term: TermId,
) -> Option<String> {
    // (1) Expression body via OperationImpl — sourced from
    // `kb.op_body_node` as a NodeOccurrence tree after WI-249.
    if let Some(body_node) = ctx.op_impls.lookup(op_sym) {
        match lower_node(ctx, &body_node) {
            Ok(expr) => return Some(if return_type == "void" {
                format!("{expr};")
            } else {
                format!("return {expr};")
            }),
            Err(e) => {
                // Surface the failure as a TODO comment in the body
                // rather than silently falling through. The user sees
                // "this op has a body but cpp-gen can't lower it yet"
                // immediately at codegen time, not as a compile error
                // about a missing body later.
                return Some(format!(
                    "// TODO: cannot lower expression body — {}\n        \
                     return {{}};", e.message,
                ));
            }
        }
    }

    // Carrier-dispatch paths (2) and (3) require a `self` first param.
    let self_param = params.first()?;
    if self_param.name != "self" {
        return None;
    }
    let arrow = if self_param.cpp_type.contains('*') { "->" } else { "." };
    let cpp_method = cpp_method_name(ctx.kb, name);

    // WI-089(a): the carrier we dispatch onto is the operation's parent sort;
    // its `Implementation.binding` (if any) names the marshalling overlay
    // family active across THIS boundary. Prepended to the active-key list so a
    // binding-keyed `TypeMapping` (webots `Vec3 -> const double *`) is selected
    // for the arguments and return below, while declared-signature lowering
    // (no binding key) leaves `Vec3` its generated struct.
    let binding = parent_qualified_name(ctx.kb, op_sym)
        .and_then(|qn| ctx.carriers.binding(&qn).map(str::to_string));
    let binding = binding.as_deref();

    // A non-self argument whose anthill type IS marshalled but declares
    // no `lower` adapter cannot be bridged into the host call — surface
    // it loudly (matching the void+lift guard below) rather than emitting
    // the un-lowered value, which would silently fail to compile (WI-088).
    // A type with no marshalled entry at all is a normal value and passes
    // through bare; only the "marshalled, but no anthill->foreign adapter"
    // case is the unhandleable one.
    for p in params.iter().skip(1) {
        if matches!(marshal_for_type(ctx, p.type_term, binding), Some(m) if m.lower.is_none()) {
            let tail = if return_type == "void" { "" } else { "\n        return {};" };
            return Some(format!(
                "// TODO: WI-088: parameter '{}' has a marshalled type with no `lower` adapter — \
                 add a `lower` to its TypeMapping fact so the anthill value converts to the \
                 host's foreign representation{tail}",
                p.name
            ));
        }
    }

    // Marshal each non-self argument (WI-088): if its anthill type has a
    // `lower` adapter, the host API wants the foreign rep, so wrap the
    // argument before passing it (`Vec3 -> const double *`). After the
    // guard above, a `None` here means the type is not marshalled at all,
    // so the argument passes through bare.
    let non_self_args = params.iter().skip(1)
        .map(|p| match marshal_for_type(ctx, p.type_term, binding).and_then(|m| m.lower) {
            Some(lower) => format!("{lower}({})", p.name),
            None => p.name.clone(),
        })
        .collect::<Vec<_>>()
        .join(", ");
    let call = format!("self{arrow}{cpp_method}({non_self_args})");

    // (2) Return marshalling (WI-088): if the return type has a `lift`
    // adapter, the host method yields the foreign rep, so wrap the call
    // to lift it back to the anthill type (`const double * -> Vec3`).
    // Takes precedence over plain dispatch — the lift applies even when
    // the return would otherwise have been body-emittable.
    if let Some(lift) = marshal_for_type(ctx, return_term, binding).and_then(|m| m.lift) {
        if return_type == "void" {
            // Nothing to lift from a void return — a `lift` here is a
            // spec error. Surface it loudly rather than emitting
            // `lift(call);`, which would discard the lifted value.
            return Some(format!(
                "// TODO: WI-088: `lift` adapter '{lift}' declared for the return type of \
                 void-returning operation '{name}' — a lift converts a returned value; \
                 drop the lift or give the operation a non-void return\n        {call};"
            ));
        }
        return Some(format!("return {lift}({call});"));
    }

    // (3) Plain carrier dispatch (no return marshalling).
    if !is_body_emittable(ctx, return_term) {
        return None;
    }
    Some(if return_type == "void" {
        format!("{call};")
    } else {
        format!("return {call};")
    })
}

/// Walk the KB's `OperationInfo` facts and return all that belong to
/// `sort_sym`'s scope. Each entry's params and return type are lowered
/// via the carrier-aware `lower_type`.
fn operations_in_sort(
    ctx: &CodegenContext,
    sort_sym: Symbol,
) -> Result<Vec<OperationSig>, CppCodegenError> {
    let kb = ctx.kb;
    let op_info_sym = match kb.try_resolve_symbol("anthill.reflect.OperationInfo") {
        Some(s) => s,
        None => return Ok(Vec::new()),
    };
    let qualified = kb.qualified_name_of(sort_sym).to_string();

    let mut out = Vec::new();
    for rid in kb.rules_by_functor(op_info_sym) {
        // WI-348: the OperationInfo head may be a *value fact* (Node-carrying)
        // for an op with a `denoted` effect (`Modify[c]`), so it can't be read
        // as a term. Match the fact to its op symbol carrier-agnostically, then
        // read its fields through the `op_info` API rather than walking the head.
        let op_sym = match anthill_core::kb::op_info::head_name_ref(kb, kb.rule_head_value(rid)) {
            Some(s) => s,
            None => continue,
        };
        // The operation's parent sort is the prefix of its qualified
        // name (test.simple.Greeter.count → test.simple.Greeter).
        // We don't use SymbolTable::scope_of because that helper finds
        // a *sibling* symbol in the same scope, not the parent.
        if parent_qualified_name(kb, op_sym) != Some(qualified.to_string()) {
            continue;
        }

        let name = kb.resolve_sym(op_sym).to_string();
        let rec = anthill_core::kb::op_info::lookup_operation_info(kb, op_sym)
            .ok_or_else(|| CppCodegenError {
                message: format!("operation '{name}' missing OperationInfo"),
            })?;

        // WI-341: the return type is carrier-agnostic; a denoted-bearing
        // (`Value::Node`) return — an op returning a `Modify`-carrying callback —
        // is unsupported by C++ codegen (never materialized).
        let return_term = match &rec.return_type {
            anthill_core::eval::Value::Term(t) => *t,
            _ => {
                return Err(CppCodegenError {
                    message: format!(
                        "operation '{name}' has a denoted-bearing return type \
                         unsupported by C++ codegen"
                    ),
                })
            }
        };
        let return_type_cpp = lower_type(ctx, return_term)?;

        let mut params = Vec::new();
        for (p_name_sym, p_type) in &rec.params {
            // WI-341 Stage A: param types are carrier-agnostic. A ground type
            // lowers as before; a denoted-bearing (`Value::Node`) callback-arrow
            // param is not supported by C++ codegen (and is never materialized).
            let p_term = match p_type {
                anthill_core::eval::Value::Term(t) => *t,
                _ => {
                    return Err(CppCodegenError {
                        message: format!(
                            "operation '{name}' parameter '{}' has a denoted-bearing \
                             type unsupported by C++ codegen",
                            kb.resolve_sym(*p_name_sym)
                        ),
                    })
                }
            };
            let cpp_type = lower_type(ctx, p_term)?;
            params.push(ParamInfo {
                name: kb.resolve_sym(*p_name_sym).to_string(),
                cpp_type,
                type_term: p_term,
            });
        }

        // Pull declared effects so we can wrap the return type for effects the
        // cpp profile realizes as `ResultWrap`. WI-089(b): this decision is now
        // FACT-DRIVEN — an effect wraps iff cpp's `EffectMapping` maps it to
        // `ResultWrap` (the keyed query), not a hardcoded "Error" string-match.
        // The cpp meaning of `ResultWrap` (`tl::expected<R, std::string>` + the
        // `<tl/expected.hpp>` include) stays here, exactly as a `TypeMapping`
        // host_type's template wrapping does. Effect labels are carrier-agnostic
        // `Value`s; a realizable label is a ground `Value::Term` (a `denoted`
        // label like `Modify[c]` is a `Value::Node`).
        let wraps_result = rec.effects.iter().any(|eff| match eff {
            anthill_core::eval::Value::Term(t) => effect_kind_short(kb, *t)
                .and_then(|name| cpp_effect_receiver(ctx, &name))
                .as_deref()
                == Some("ResultWrap"),
            _ => false,
        });
        let return_type_cpp = if wraps_result {
            ctx.requested_includes.borrow_mut()
                .insert("#include <tl/expected.hpp>".to_string());
            format!("tl::expected<{return_type_cpp}, std::string>")
        } else {
            return_type_cpp
        };
        let body = synthesise_body_for(ctx, op_sym, &name, &params, &return_type_cpp, return_term);
        out.push(OperationSig { name, params, return_type_cpp, body });
    }

    out.sort_by(|a, b| a.name.cmp(&b.name));
    Ok(out)
}

// ── Namespace header emission ────────────────────────────────────────

/// Emit a complete C++ header file for every entity declared directly
/// under the given anthill namespace. Output includes `#pragma once`,
/// the minimum set of standard-library includes implied by emitted
/// types, and a `namespace foo::bar { ... }` wrapper.
///
/// Anthill `foo.bar.Baz` becomes C++ `foo::bar` (C++17 nested-namespace
/// syntax). Entities are emitted in qualified-name order for
/// determinism.
///
/// Returns an error if no entities are found under the namespace
/// (catches typos / wrong namespace strings before they produce empty
/// headers).
/// Short names of all traits classes (sorts with operations) declared
/// directly under `namespace`. Mirrors the third element of
/// `classify_namespace`'s tuple but lifts it to a public API so
/// callers driving project-layout scaffolding (one controller per
/// traits class) can iterate the targets without re-implementing the
/// classification.
pub fn traits_classes_in_namespace(kb: &KnowledgeBase, namespace: &str) -> Vec<String> {
    let ctx = CodegenContext::new(kb);
    let (_, _, traits) = classify_namespace(&ctx, namespace);
    traits.iter()
        .map(|sym| short_name_of(kb.qualified_name_of(*sym)).to_string())
        .collect()
}

/// One `fact Generated(...)` declaration: an anthill sort the
/// project intends to lower into a host artifact. Read by project-
/// layout writers as the spec-driven source of truth for "what to
/// scaffold" — replacing the older heuristic that any traits class
/// in a namespace is a controller.
#[derive(Debug, Clone)]
pub struct GeneratedTarget {
    pub source: String,
    pub artifact: String,
    pub language: String,
    pub profile: Option<String>,
    pub kind: String,
    pub description: Option<String>,
}

/// Scan the KB for `Generated(...)` facts. Returns every fact
/// regardless of language so callers can filter (cpp-gen wants
/// `language = "cpp"`; a Rust scaffold would want `"rust"`). Empty
/// when nothing is declared.
pub fn generated_targets(kb: &KnowledgeBase) -> Vec<GeneratedTarget> {
    let sym = match kb.try_resolve_symbol("anthill.realization.Generated")
        .or_else(|| kb.try_resolve_symbol("Generated"))
    {
        Some(s) => s,
        None => return Vec::new(),
    };
    let mut out = Vec::new();
    for rid in kb.rules_by_functor(sym) {
        let head = kb.rule_head(rid);
        let Some(source) = named_arg(kb, head, "source").and_then(|t| as_string(kb, t)) else { continue };
        let Some(artifact) = named_arg(kb, head, "artifact").and_then(|t| as_string(kb, t)) else { continue };
        let Some(language) = named_arg(kb, head, "language").and_then(|t| as_string(kb, t)) else { continue };
        let Some(kind) = named_arg(kb, head, "kind").and_then(|t| as_string(kb, t)) else { continue };
        let profile = named_optional_string(kb, head, "profile");
        let description = named_optional_string(kb, head, "description");
        out.push(GeneratedTarget { source, artifact, language, profile, kind, description });
    }
    out.sort_by(|a, b| a.source.cmp(&b.source));
    out
}

/// Unwrap an `Option<String>` term: `some("foo")` → `Some("foo")`,
/// `none` → `None`. Canonical layout per the prelude is
/// `entity some(value: T)`, so we read the `value` named arg first
/// and fall back to the first positional slot — the typing pass
/// can leave the named slot as an unbound Var while the actual
/// string lands positionally. We do *not* scan every named slot;
/// a future `some(value: ?, debug: "x")` shape would otherwise
/// silently return the wrong string.
fn extract_optional_string(kb: &KnowledgeBase, term: TermId) -> Option<String> {
    if let Some(s) = as_string(kb, term) { return Some(s); }
    let Term::Fn { functor, named_args, pos_args } = kb.get_term(term) else {
        return None;
    };
    let qn = kb.qualified_name_of(*functor);
    if qn != "anthill.prelude.Option.some" && qn != "Option.some" {
        return None;
    }
    if let Some(v) = find_named(kb, named_args, "value").and_then(|t| as_string(kb, t)) {
        return Some(v);
    }
    pos_args.first().and_then(|t| as_string(kb, *t))
}

