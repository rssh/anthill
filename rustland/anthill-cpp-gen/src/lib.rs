/// Lower an anthill type term to a C++ type expression.
///
/// Field types in entity declarations are wrapped as
/// `sort_ref(name: <symbol>)` by the loader. Bare `Term::Ref` /
/// `Term::Ident` are also accepted for robustness. Parameterized
/// types (`List[T = X]`, `Option[T = X]`, ...) appear as `Term::Fn`
/// with the parameterized sort as functor and the bindings as
/// named args; the template name is resolved via the keyed `TypeMapping`
/// query (`cpp_base_host_type`).
// ── Expression lowering ───────────────────────────────────────────

/// Lower an anthill expression term (the proposal-026 reflected
/// `anthill.reflect.Expr.*` form) to a C++ expression string.
///
/// String literals are escaped with the same convention the printer
/// uses (\" \\ \n \r \t).
fn lower_node(
    ctx: &CodegenContext,
    occ: &std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>,
) -> Result<String, CppCodegenError> {
    use anthill_core::kb::node_occurrence::{Expr, NodeKind};
    let kb = ctx.kb;
    let expr = match &occ.kind {
        NodeKind::Expr { expr, .. } => expr,
        NodeKind::RuleHead { .. } => return Err(CppCodegenError {
            message: "rule-head occurrence in expression body — \
                      should never reach cpp-gen".into(),
        }),
        NodeKind::Pattern(_) => return Err(CppCodegenError {
            message: "pattern occurrence in expression body — \
                      should be consumed by parent let/lambda/match (WI-318)".into(),
        }),
        NodeKind::Type(_) | NodeKind::EffectExpr(_) => return Err(CppCodegenError {
            message: "Type/EffectExpr occurrence in expression body — \
                      type-level data, never an evaluation target (WI-342)".into(),
        }),
    };
    match expr {
        Expr::Const(lit) => Ok(lower_literal(lit)),
        Expr::Ref(sym) | Expr::Ident(sym) => {
            let qn = kb.qualified_name_of(*sym);
            // Stdlib wrapper constants used as values (`none` on its
            // own line) — lower the qualified name through the same
            // table that handles `none()` / `some(...)`. Math
            // constants used as bare values: `pi`, `e`, `tau`.
            if let Some(rendered) = lower_stdlib_constant(qn) {
                return Ok(rendered);
            }
            if let Some(rendered) = render_as_math_constant(qn) {
                return Ok(rendered);
            }
            if let Some(rendered) = render_as_float_special(qn) {
                return Ok(rendered);
            }
            // WI-536: a reference to a term-level const resolves to its named
            // constant (struct member or namespace companion), not the bare
            // short name (which would not be in scope at the C++ use site).
            if kb.kind_of(*sym) == Some(SymbolKind::Const) {
                return Ok(const_ref_cpp(ctx, *sym));
            }
            Ok(short_name_of(qn).to_string())
        }
        Expr::VarRef { name } => {
            let qn = kb.qualified_name_of(*name);
            if let Some(rendered) = lower_stdlib_constant(qn) {
                return Ok(rendered);
            }
            if let Some(rendered) = render_as_math_constant(qn) {
                return Ok(rendered);
            }
            if let Some(rendered) = render_as_float_special(qn) {
                return Ok(rendered);
            }
            let short = short_name_of(qn);
            // A let-local / pattern binding shadows a same-named const.
            if let Some(access) = ctx.lookup_value_binding(short) {
                return Ok(access);
            }
            // WI-536: otherwise a const reference resolves to its named constant.
            if kb.kind_of(*name) == Some(SymbolKind::Const) {
                return Ok(const_ref_cpp(ctx, *name));
            }
            Ok(short.to_string())
        }
        Expr::Apply { functor, pos_args, named_args, .. } => {
            let fn_qn = kb.qualified_name_of(*functor).to_string();
            let fn_short = short_name_of(&fn_qn).to_string();

            // field_access(object, field) is the desugared form of
            // `obj.field` — emit dot syntax. The second arg is a
            // var_ref pointing at the field identifier.
            if fn_qn == "anthill.reflect.field_access" {
                let combined = combined_args(pos_args, named_args);
                if combined.len() != 2 {
                    return Err(CppCodegenError {
                        message: format!(
                            "field_access expects 2 args, got {}",
                            combined.len()
                        ),
                    });
                }
                let object = lower_node(ctx, combined[0])?;
                let field = field_name_from_node(kb, combined[1])?;
                return Ok(format!("{object}.{field}"));
            }
            // Error.raise(e) ⇒ tl::make_unexpected(e). The op's return
            // type was wrapped in tl::expected by operations_in_sort,
            // so this slots in as a branch result or whole body.
            if fn_qn == "anthill.prelude.Error.raise"
                || fn_qn == "Error.raise"
            {
                let combined = combined_args(pos_args, named_args);
                if combined.len() != 1 {
                    return Err(CppCodegenError {
                        message: format!(
                            "Error.raise expects 1 arg, got {}",
                            combined.len()
                        ),
                    });
                }
                ctx.requested_includes.borrow_mut()
                    .insert("#include <tl/expected.hpp>".to_string());
                let payload = lower_node(ctx, combined[0])?;
                return Ok(format!("tl::make_unexpected({payload})"));
            }
            // Stdlib wrapper constructors that don't map 1:1 to a C++
            // struct (Option/Result) — handled before the generic
            // entity-literal path below.
            if let Some(rendered) = lower_stdlib_wrapper_node(ctx, &fn_qn, pos_args, named_args)? {
                return Ok(rendered);
            }
            // Entity-constructor case: when the loader didn't wrap an
            // entity application as `Expr.constructor` (a `sort X` /
            // `entity X` name clash), the apply still points at an
            // entity functor — emit brace-init instead of a call.
            if kb.entity_field_types(*functor).is_some() {
                return lower_constructor_literal_node(ctx, *functor, pos_args, named_args);
            }
            let mut args = Vec::new();
            for a in combined_args(pos_args, named_args) {
                args.push(lower_node(ctx, a)?);
            }
            if let Some(rendered) = render_as_operator(&fn_qn, &args) {
                return Ok(rendered);
            }
            if let Some(rendered) = render_as_math_call(ctx, &fn_qn, &args) {
                return Ok(rendered);
            }
            if let Some(rendered) = render_as_indexed_seq(&fn_qn, &args) {
                return Ok(rendered);
            }
            if args.is_empty() {
                if let Some(rendered) = render_as_math_constant(&fn_qn) {
                    return Ok(rendered);
                }
            }
            Ok(format!("{fn_short}({})", args.join(", ")))
        }
        Expr::Constructor { name, pos_args, named_args } => {
            let name_qn = kb.qualified_name_of(*name).to_string();
            // List/Tuple/Set literals → uniform brace-init.
            if matches!(name_qn.as_str(),
                "anthill.reflect.ListLiteral"
                | "anthill.reflect.TupleLiteral"
                | "anthill.reflect.SetLiteral")
            {
                let mut parts = Vec::new();
                for a in combined_args(pos_args, named_args) {
                    parts.push(lower_node(ctx, a)?);
                }
                return Ok(format!("{{{}}}", parts.join(", ")));
            }
            if let Some(rendered) = lower_stdlib_wrapper_node(ctx, &name_qn, pos_args, named_args)? {
                return Ok(rendered);
            }
            if kb.entity_field_types(*name).is_some() {
                return lower_constructor_literal_node(ctx, *name, pos_args, named_args);
            }
            Err(CppCodegenError {
                message: format!(
                    "constructor '{name_qn}' is neither a known \
                     entity nor a recognised collection literal"
                ),
            })
        }
        Expr::Match { scrutinee, branches } => {
            let scrutinee_s = lower_node(ctx, scrutinee)?;
            if branches.is_empty() {
                return Err(CppCodegenError {
                    message: "match_expr with no branches".into(),
                });
            }
            lower_match_branches_node(ctx, &scrutinee_s, branches)
        }
        Expr::If { condition, then_branch, else_branch } => {
            let cond_s = lower_node(ctx, condition)?;
            let then_s = lower_node(ctx, then_branch)?;
            let else_s = lower_node(ctx, else_branch)?;
            Ok(format!("({cond_s} ? {then_s} : {else_s})"))
        }
        Expr::Let { .. } => lower_let_chain_node(ctx, occ),
        Expr::Lambda { param, body } => {
            // WI-318: param is now a Pattern-kind occurrence.
            let pname = pattern_var_name_occ(kb, param)?;
            let body_s = lower_node(ctx, body)?;
            Ok(format!("[=](auto {pname}) {{ return {body_s}; }}"))
        }
        Expr::Proof { body, .. } => {
            // WI-538: an in-body proof is a type-level construct with no
            // runtime effect — lower to just the continuation.
            lower_node(ctx, body)
        }
        Expr::ListLit(elems) => {
            let mut parts = Vec::new();
            for e in elems.iter() {
                parts.push(lower_node(ctx, e)?);
            }
            Ok(format!("{{{}}}", parts.join(", ")))
        }
        Expr::SetLit(elems) => {
            let mut parts = Vec::new();
            for e in elems.iter() {
                parts.push(lower_node(ctx, e)?);
            }
            Ok(format!("{{{}}}", parts.join(", ")))
        }
        Expr::TupleLit { positional, named } => {
            let mut parts = Vec::new();
            for e in positional.iter() {
                parts.push(lower_node(ctx, e)?);
            }
            for (_, e) in named.iter() {
                parts.push(lower_node(ctx, e)?);
            }
            Ok(format!("{{{}}}", parts.join(", ")))
        }
        Expr::HoApply { .. }
        | Expr::Instantiation { .. }
        | Expr::ApplyWithin { .. }
        | Expr::HoApplyWithin { .. }
        | Expr::ConstructorWithin { .. }
        | Expr::LambdaWithin { .. }
        | Expr::RequirementAtSort { .. }
        | Expr::ConstructRequirement { .. } => Err(CppCodegenError {
            message: "post-elaboration / higher-order Expr variant not \
                      supported by cpp-gen profile yet".into(),
        }),
        Expr::Var(v) => {
            let name_sym = match v {
                anthill_core::kb::term::Var::Global(vid) => vid.name(),
                anthill_core::kb::term::Var::DeBruijn(_) => return Err(CppCodegenError {
                    message: "DeBruijn var in expression body — \
                              should have been opened by the loader".into(),
                }),
                anthill_core::kb::term::Var::Rigid(_) => return Err(CppCodegenError {
                    message: "Rigid var in expression body — only valid \
                              during proof discharge, not codegen".into(),
                }),
            };
            let name = kb.resolve_sym(name_sym);
            if let Some(access) = ctx.lookup_value_binding(name) {
                return Ok(access);
            }
            Err(CppCodegenError {
                message: format!(
                    "logic variable '?{name}' appeared in an expression body — \
                     belongs in rule bodies, not operation bodies"
                ),
            })
        }
        Expr::Bottom => Err(CppCodegenError {
            message: "cannot lower Bottom (⊥) to a C++ expression".into(),
        }),
        // WI-278: a pre-dispatch `dot_apply` is rewritten to an `Apply` /
        // field access by the `[simp]` dot rules before codegen; one reaching
        // cpp-gen is an unresolved method call.
        Expr::DotApply { name, .. } => Err(CppCodegenError {
            message: format!(
                "unresolved method call '.{}' reached cpp-gen — the [simp] dot \
                 rules should have rewritten it",
                kb.resolve_sym(*name),
            ),
        }),
    }
}

/// Concatenate positional + named arg slices into a single iteration
/// order for cpp-gen: pos first, then named. The caller is responsible
/// for routing field-keyed arguments through `find_in_named` when it
/// actually needs the field name (e.g. entity-literal field ordering).
fn combined_args<'a>(
    pos_args: &'a [std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>],
    named_args: &'a [(Symbol, std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>)],
) -> Vec<&'a std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>> {
    let mut out: Vec<&std::rc::Rc<_>> = Vec::with_capacity(pos_args.len() + named_args.len());
    for a in pos_args.iter() { out.push(a); }
    for (_, a) in named_args.iter() { out.push(a); }
    out
}

/// Lower a let-chain `let x = v; body` into a single IIFE. Nested
/// `Expr::Let` in `body` is flattened into one IIFE with multiple
/// `auto` declarations to avoid one lambda per binding.
///
/// Wildcard pattern (`let _ = expr; body`) emits the expression as a
/// discarded statement — the codegen seam for side-effecting calls
/// (`Modify[…]`, `Console.println`, …) that return Unit, since
/// `auto _ = expr;` would not compile when expr is `void`.
fn lower_let_chain_node(
    ctx: &CodegenContext,
    root: &std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>,
) -> Result<String, CppCodegenError> {
    use anthill_core::kb::node_occurrence::{Expr, NodeKind};
    enum Slot {
        Bind(String, String),
        Discard(String),
    }
    let kb = ctx.kb;
    let mut slots: Vec<Slot> = Vec::new();
    let mut current = root.clone();
    let body_node = loop {
        let next_body = match &current.kind {
            NodeKind::Expr { expr: Expr::Let { pattern, value, body, .. }, .. } => {
                // WI-318: pattern is a Pattern-kind occurrence.
                use anthill_core::kb::node_occurrence::Pattern;
                let is_wildcard = matches!(pattern.as_pattern(), Some(Pattern::Wildcard));
                if !is_wildcard {
                    let bind_name = pattern_var_name_occ(kb, pattern)?;
                    check_recursive_lambda_node(kb, value, &bind_name)?;
                    let val_s = lower_node(ctx, value)?;
                    slots.push(Slot::Bind(bind_name, val_s));
                } else {
                    let val_s = lower_node(ctx, value)?;
                    slots.push(Slot::Discard(val_s));
                }
                body.clone()
            }
            _ => break current,
        };
        current = next_body;
    };
    let body_s = lower_node(ctx, &body_node)?;
    let mut out = String::from("[&]() { ");
    for slot in &slots {
        match slot {
            Slot::Bind(name, val) => {
                out.push_str(&format!("auto {name} = {val}; "));
            }
            Slot::Discard(val) => {
                out.push_str(&format!("{val}; "));
            }
        }
    }
    out.push_str(&format!("return {body_s}; }}()"));
    Ok(out)
}

/// Extract the field name from the 2nd arg of `field_access(object, field)`.
/// Two producers feed this, with different field-name encodings:
///   - the typer's `DotApply` field-fallback (WI-279/WI-490) synthesizes the
///     field as a `Const(String("<field>"))` — the form the eval-side
///     `reflect_field_access` reads (`Value::Str`), so the canonical one;
///   - the loader/converter `field_access` builtin wraps the field identifier
///     as a `var_ref`/`Ref`/`Ident` symbol reference.
/// Accept both so cpp-gen lowers a field access regardless of which path built
/// the body node.
fn field_name_from_node(
    kb: &KnowledgeBase,
    occ: &std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>,
) -> Result<String, CppCodegenError> {
    use anthill_core::kb::node_occurrence::{Expr, NodeKind};
    if let NodeKind::Expr { expr, .. } = &occ.kind {
        match expr {
            Expr::Const(Literal::String(s)) => return Ok(s.clone()),
            Expr::VarRef { name } | Expr::Ref(name) | Expr::Ident(name) => {
                let qn = kb.qualified_name_of(*name);
                return Ok(short_name_of(qn).to_string());
            }
            _ => {}
        }
    }
    Err(CppCodegenError {
        message: "field_access: 2nd arg isn't a string literal or symbol reference".into(),
    })
}

/// Reject `let f = lambda(?x) -> ... f ...` — anonymous self-recursion
/// against the bound name. Profile cpp17-stl emits
/// `[=](auto x) { return body; }`, which gives the lambda no
/// self-reference. Workarounds (Y-combinator, `std::function`) either
/// bloat the call site or introduce refcounted heap closures —
/// neither is "RAII only". The user should lift the body to a named
/// operation, which compiles to a regular C++ function and recurses
/// cleanly by name.
fn check_recursive_lambda_node(
    kb: &KnowledgeBase,
    val: &std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>,
    bind_name: &str,
) -> Result<(), CppCodegenError> {
    use anthill_core::kb::node_occurrence::{Expr, NodeKind};
    let body = match &val.kind {
        NodeKind::Expr { expr: Expr::Lambda { body, .. }, .. } => body,
        _ => return Ok(()),
    };
    if !node_references_name(kb, body, bind_name) {
        return Ok(());
    }
    Err(CppCodegenError {
        message: format!(
            "recursive anonymous lambda not supported in cpp17-stl \
             profile (binder '{bind_name}' referenced inside its own \
             lambda body) — lift the body to a named operation, \
             which lowers to a regular C++ function and recurses \
             cleanly by name"
        ),
    })
}

/// Recursive name-reference scan on a NodeOccurrence Expr tree —
/// compares the short name of every `Ref` / `Ident` / `VarRef` /
/// `Apply.functor` / `Constructor.name` against `target`. Used only
/// by `check_recursive_lambda_node` to detect anonymous self-recursion.
fn node_references_name(
    kb: &KnowledgeBase,
    occ: &std::rc::Rc<anthill_core::kb::node_occurrence::NodeOccurrence>,
    target: &str,
) -> bool {
    use anthill_core::kb::node_occurrence::{Expr, NodeKind};
    let expr = match &occ.kind {
        NodeKind::Expr { expr, .. } => expr,
        _ => return false,
    };
    let matches_sym = |s: Symbol| short_name_of(kb.qualified_name_of(s)) == target;
    match expr {
        Expr::Ref(s) | Expr::Ident(s) | Expr::VarRef { name: s } => matches_sym(*s),
        Expr::Apply { functor, pos_args, named_args, .. } => {
            matches_sym(*functor)
                || pos_args.iter().any(|a| node_references_name(kb, a, target))
                || named_args.iter().any(|(_, a)| node_references_name(kb, a, target))
        }
        Expr::Constructor { name, pos_args, named_args } => {
            matches_sym(*name)
                || pos_args.iter().any(|a| node_references_name(kb, a, target))
                || named_args.iter().any(|(_, a)| node_references_name(kb, a, target))
        }
        Expr::If { condition, then_branch, else_branch } => {
            node_references_name(kb, condition, target)
                || node_references_name(kb, then_branch, target)
                || node_references_name(kb, else_branch, target)
        }
        Expr::Let { value, body, .. } => {
            node_references_name(kb, value, target)
                || node_references_name(kb, body, target)
        }
        // WI-538: a proof is type-transparent — its continuation `body`
        // (and the conclude goal) may reference the name.
        Expr::Proof { conclude, body, .. } => {
            conclude.as_ref().is_some_and(|c| node_references_name(kb, c, target))
                || node_references_name(kb, body, target)
        }
        Expr::Match { scrutinee, branches } => {
            node_references_name(kb, scrutinee, target)
                || branches.iter().any(|b| node_references_name(kb, &b.body, target))
        }
        Expr::Lambda { body, .. } => node_references_name(kb, body, target),
        Expr::ListLit(es) | Expr::SetLit(es) => {
            es.iter().any(|a| node_references_name(kb, a, target))
        }
        Expr::TupleLit { positional, named } => {
            positional.iter().any(|a| node_references_name(kb, a, target))
                || named.iter().any(|(_, a)| node_references_name(kb, a, target))
        }
        _ => false,
    }
}

/// Find a named arg in a Term::Fn's named_args slice by string key.
fn find_named(kb: &KnowledgeBase, named_args: &[(Symbol, TermId)], name: &str) -> Option<TermId> {
    named_args.iter()
        .find(|(s, _)| kb.resolve_sym(*s) == name)
        .map(|(_, v)| *v)
}

/// Read `sort T = ?` declarations inside a sort's body. Stable
/// alphabetical order — used both to emit `template<typename …>`
/// prefixes and to drive type-parameter substitution in `lower_type`.
fn type_params_of(kb: &KnowledgeBase, sort_sym: Symbol) -> Vec<String> {
    kb.type_params_of_sort(sort_sym)
}

/// Reserved C++ keywords that would clash if used as a template
/// parameter identifier. The list is conservative — anything that
/// could be a type/identifier in a `template<typename X>` slot.
const CPP_KEYWORDS: &[&str] = &[
    "alignas", "alignof", "and", "and_eq", "asm", "auto", "bitand", "bitor",
    "bool", "break", "case", "catch", "char", "char16_t", "char32_t",
    "char8_t", "class", "compl", "concept", "const", "consteval", "constexpr",
    "constinit", "const_cast", "continue", "co_await", "co_return", "co_yield",
    "decltype", "default", "delete", "do", "double", "dynamic_cast", "else",
    "enum", "explicit", "export", "extern", "false", "float", "for", "friend",
    "goto", "if", "inline", "int", "long", "module", "mutable", "namespace",
    "new", "noexcept", "not", "not_eq", "nullptr", "operator", "or", "or_eq",
    "private", "protected", "public", "register", "reinterpret_cast", "requires",
    "return", "short", "signed", "sizeof", "static", "static_assert",
    "static_cast", "struct", "switch", "template", "this", "thread_local",
    "throw", "true", "try", "typedef", "typeid", "typename", "union",
    "unsigned", "using", "virtual", "void", "volatile", "wchar_t", "while",
    "xor", "xor_eq",
];

/// Pick a unique C++ identifier for a type parameter. Keeps the source
/// name when it's safe; otherwise appends `0..N` until a free slot is
/// found. `taken` is updated with the chosen name.
fn canonicalise_param_name(source_name: &str, taken: &mut std::collections::HashSet<String>) -> String {
    let base = source_name.trim_start_matches('?');
    let needs_suffix = CPP_KEYWORDS.contains(&base) || taken.contains(base);
    let chosen = if needs_suffix {
        let mut n = 0;
        loop {
            let candidate = format!("{base}{n}");
            if !taken.contains(&candidate) && !CPP_KEYWORDS.contains(&candidate.as_str()) {
                break candidate;
            }
            n += 1;
        }
    } else {
        base.to_string()
    };
    taken.insert(chosen.clone());
    chosen
}

/// Build the `(template prefix, name → cpp_param)` pair for a sort.
/// Empty prefix and empty map when the sort has no type parameters.
fn template_prefix_for_sort(
    kb: &KnowledgeBase,
    sort_sym: Symbol,
) -> (String, std::collections::HashMap<String, String>) {
    let params = type_params_of(kb, sort_sym);
    if params.is_empty() {
        return (String::new(), std::collections::HashMap::new());
    }
    let mut taken = std::collections::HashSet::new();
    let mut mapping = std::collections::HashMap::new();
    let mut cpp_names = Vec::with_capacity(params.len());
    for p in &params {
        let cpp = canonicalise_param_name(p, &mut taken);
        mapping.insert(p.clone(), cpp.clone());
        cpp_names.push(cpp);
    }
    let prefix = format!(
        "template<{}>\n",
        cpp_names.iter()
            .map(|n| format!("typename {n}"))
            .collect::<Vec<_>>()
            .join(", ")
    );
    (prefix, mapping)
}

/// Classify a declared effect term into a short, codegen-relevant
/// kind. `Error[T = …]`, `sort_ref(Error)`, and bare `Error` all
/// return `"Error"`; `Modify[T = …]` returns `"Modify"`. Unknown
/// effects map to their short name. Returns `None` for terms that
/// don't look like an effect reference at all.
fn effect_kind_short(kb: &KnowledgeBase, term: TermId) -> Option<String> {
    let short_of = |sym: Symbol| short_name_of(kb.qualified_name_of(sym)).to_string();
    // Unwrap `sort_ref(...)` and `parameterized(base: ..., bindings: ...)`
    // envelopes via the existing helpers — pre-typing forms like
    // `Error[T = X]` or `Modify(self)` fall through to the bare
    // functor short name.
    if let Some(sym) = extract_sort_ref_sym(kb, &TermIdView(term)) {
        return Some(short_of(sym));
    }
    if let Some((base, _)) = unpack_parameterized(kb, term) {
        return Some(short_of(base));
    }
    // Bare functor/ref short name — shared with `EffectMapping.receiver` reads.
    functor_or_ref_short(kb, term)
}

/// Short name of a term that is a bare constructor reference: `Ref`/`Ident` for
/// a nullary entity, `Fn` for a payload-bearing one. The common tail of
/// `effect_kind_short` (after its sort_ref/parameterized unwrap) and the reader
/// for an `EffectMapping.receiver` (a `ReceiverForm` constructor). `None` for
/// terms that aren't a constructor reference at all.
fn functor_or_ref_short(kb: &KnowledgeBase, term: TermId) -> Option<String> {
    match kb.get_term(term) {
        Term::Ref(s) | Term::Ident(s) => Some(short_name_of(kb.qualified_name_of(*s)).to_string()),
        Term::Fn { functor, .. } => Some(short_name_of(kb.qualified_name_of(*functor)).to_string()),
        _ => None,
    }
}

